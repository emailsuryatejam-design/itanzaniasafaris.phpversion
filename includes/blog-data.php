function getBlogBySlug($slug) {
  global $blogPosts;
  foreach ($blogPosts as $post) {
    if ($post['slug'] === $slug) return $post;
  }
  return null;
}

function getFeaturedPosts() {
  global $blogPosts;
  return array_filter($blogPosts, fn($p) => $p['featured']);
}

function getRelatedPosts($currentSlug, $category, $limit = 3) {
  global $blogPosts;
  $related = array_filter($blogPosts, fn($p) => $p['slug'] !== $currentSlug && $p['category'] === $category);
  if (count($related) < $limit) {
    $others = array_filter($blogPosts, fn($p) => $p['slug'] !== $currentSlug && $p['category'] !== $category);
    $related = array_merge($related, array_slice($others, 0, $limit - count($related)));
  }
  return array_slice($related, 0, $limit);
}
