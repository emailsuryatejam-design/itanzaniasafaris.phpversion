<h2>Tanzania's Two Safari Seasons Explained</h2>
<p>Tanzania sits just south of the equator, giving it a tropical climate moderated by altitude (Arusha sits at 1,400m, the Serengeti plains at 1,500m, the Ngorongoro Crater rim at 2,300m). Two main seasons drive everything:</p>
<ul>
  <li><strong>Dry season (June–October):</strong> Little to no rain. Vegetation thins, wildlife concentrates around rivers and waterholes, roads are solid. Highest prices and crowds — but unbeatable game viewing.</li>
  <li><strong>Wet season (November–May):</strong> Divided into short rains (Nov–Dec) and long rains (March–May). Landscapes are lush and green, newborn animals everywhere, migratory birds arrive, prices drop 20–40%. Jan–Feb (calving season) is actually one of the most action-packed times of year despite being "wet season."</li>
</ul>
<p>Neither season is wrong. The best time to visit Tanzania depends entirely on what you want to experience — river crossings, calving season, value pricing, or empty parks.</p>

