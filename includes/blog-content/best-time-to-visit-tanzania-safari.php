<h2>Tanzania Safari Weather: What to Expect</h2>
<p>Tanzania's altitude keeps temperatures comfortable year-round. The Serengeti plains rarely exceed 30°C even in the hottest months. Key weather facts:</p>
<ul>
  <li><strong>Long rains:</strong> Late March to May — heavy, prolonged rain most days. Some roads impassable.</li>
  <li><strong>Short rains:</strong> November to mid-December — brief afternoon showers only. Game drives unaffected.</li>
  <li><strong>Dry season:</strong> June to October — virtually zero rain. Early mornings can be cold (10–15°C) on the Serengeti.</li>
  <li><strong>Ngorongoro Crater rim:</strong> Can drop below 5°C at night year-round due to altitude (2,300m). Bring layers.</li>
</ul>

