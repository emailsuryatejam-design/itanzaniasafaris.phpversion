<p>坦桑尼亚游猎的行李准备需要比一般度假更多的心思。一天之内，您可能经历尘土飞扬的丛林道路、强烈的赤道阳光、凉爽的高原清晨，以及热带阵雨——有时这些都在同一天发生。本专业整理的行李清单确保您装备齐全，同时避免超重，轻装上阵，畅享东非冒险之旅。</p>

<h2>服装篇：游猎穿什么</h2>
<p>游猎服装应选择中性大地色系、轻薄透气、速干易洗的材质。避免穿白色（容易吸引采采蝇，且容易脏污）和深蓝色或黑色（吸热强，同样吸引采采蝇）。</p>
<p><strong>推荐服装：</strong></p>
<ul>
  <li><strong>3至4件轻薄上衣</strong>，选择卡其色、橄榄绿、棕褐色或鼠尾草绿。长袖可提供防晒和防虫保护。</li>
  <li><strong>2至3条游猎长裤</strong>——可拆卸式两穿裤最为实用</li>
  <li><strong>1件保暖抓绒或外套</strong>——恩戈罗恩戈罗火山口边缘（海拔2,235米）的清晨气温可低至5摄氏度</li>
  <li><strong>1件雨衣</strong>——轻薄可折叠款，绿季必备</li>
  <li><strong>1顶宽檐帽</strong>——开顶游猎车上防晒的关键装备</li>
  <li><strong>舒适徒步鞋</strong>——包覆脚趾、已磨合好的鞋子，适合自然徒步和营地行走</li>
  <li><strong>凉鞋或拖鞋</strong>——在旅馆或营地放松时穿着</li>
  <li><strong>厚袜子和睡衣</strong>——高原营地夜晚可能颇为寒冷</li>
  <li><strong>泳衣</strong>——许多旅馆设有游泳池</li>
</ul>
<p><strong>重要提示：</strong>坦桑尼亚国内小飞机的行李限额通常为软质行李箱15至20公斤（不接受硬质行李箱）。请精简打包，轻装出行。</p>

