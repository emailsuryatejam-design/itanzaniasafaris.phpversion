<h2>Malariaprophylaxe</h2>
<p>Malaria ist in ganz Tansania verbreitet, einschließlich Safaridestinationen und Sansibar. Vorbeugung ist unerlässlich:</p>
<p><strong>Optionen für Malariamittel:</strong></p>
<ul>
  <li><strong>Malarone (Atovaquon/Proguanil):</strong> Die beliebteste Wahl. Beginn 1–2 Tage vorher, Fortsetzung 7 Tage nach Rückkehr. Minimale Nebenwirkungen.</li>
  <li><strong>Doxycyclin:</strong> Günstige Alternative. Beginn 1–2 Tage vorher, Fortsetzung 28 Tage nach Rückkehr. Kann Lichtempfindlichkeit verursachen.</li>
  <li><strong>Mefloquin (Lariam):</strong> Wöchentliche Einnahme, aber mögliche neuropsychiatrische Nebenwirkungen. Bitte mit Ihrem Arzt besprechen.</li>
</ul>
<p><strong>Weitere Malariapräventionsmaßnahmen:</strong></p>
<ul>
  <li>DEET-haltiges Insektenrepellent (30%+) auf exponierte Haut auftragen, besonders in der Dämmerung</li>
  <li>Abends lange Ärmel und Hosen tragen</li>
  <li>Unter einem Moskitonetz schlafen (in nahezu allen Safari-Camps und Lodges vorhanden)</li>
  <li>Moskitospiralen oder Steckdosen-Repellents im Zimmer verwenden</li>
</ul>

<h2>Reiseversicherung</h2>
<p>Eine umfassende Reiseversicherung ist für Tansania nicht gesetzlich vorgeschrieben, wird aber dringend empfohlen. Eine gute Police sollte Folgendes umfassen:</p>
<ul>
  <li><strong>Medizinische Evakuierungsdeckung</strong> – Entscheidend für Safarigebiete weit entfernt von Krankenhäusern. Ein Hubschraubertransport aus der Serengeti nach Nairobi kann 10.000+ USD kosten.</li>
  <li><strong>Notfallmedizinische Versorgung</strong> – Mindestens 100.000 USD Deckung für Krankenhausaufenthalte</li>
  <li><strong>Reiserücktritt/-unterbrechung</strong> – Schützt Ihre Investition, wenn sich Pläne ändern</li>
  <li><strong>Gepäckverlust/-verspätung</strong> – Inlandsflüge in Kleinflugzeugen können gelegentlich Gepäck fehlleiten</li>
  <li><strong>Höhendeckung</strong> – Beim <a href="blog-post.php?slug=mount-kilimanjaro-climbing-guide">Besteigen des Kilimandscharo</a> sicherstellen, dass die Police Höhen bis 6.000 m abdeckt</li>
</ul>

<h2>Weitere Gesundheitstipps für Tansania</h2>
<ul>
  <li><strong>Wasser:</strong> Nur Flaschenwasser oder gereinigtes Wasser trinken. Ihr Safarianbieter stellt sicheres Trinkwasser bereit.</li>
  <li><strong>Essen:</strong> In Ihrer Lodge oder Ihrem Camp essen, wo Zubereitungsstandards eingehalten werden. Straßenessen meiden, außer Sie haben einen robusten Magen.</li>
  <li><strong>Sonnenschutz:</strong> Die äquatoriale Sonne ist intensiv, besonders in der Höhe. LSF 50+, Hut und Sonnenbrille sind unverzichtbar.</li>
  <li><strong>Höhe:</strong> Der Ngorongoro-Kraterrand liegt auf 2.235 m und der Kilimandscharo erreicht 5.895 m. Geben Sie sich Zeit zur Akklimatisierung, wenn Sie vom Meeresspiegel kommen.</li>
</ul>

