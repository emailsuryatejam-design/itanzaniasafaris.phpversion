<p>Die Planung einer Tansania-Safari umfasst mehr als die Auswahl von Parks und Lodges. Das Verständnis der Einreisebestimmungen, Visaverfahren und Gesundheitsvorbereitungen ist für eine reibungslose Reise unerlässlich. Dieser Leitfaden behandelt alles von der Visabeantragung über Impfungen bis hin zur Malariaprophylaxe und Reiseversicherung, damit Sie vollständig vorbereitet in Tansania ankommen.</p>

<h2>Visumanforderungen für Tansania</h2>
<p>Die meisten internationalen Besucher benötigen ein Visum zur Einreise nach Tansania. Die gute Nachricht ist, dass der Prozess unkompliziert ist und online oder bei der Einreise abgewickelt werden kann.</p>

<h3>E-Visum (Empfohlen)</h3>
<p>Tansanias elektronisches Visasystem ermöglicht es Ihnen, sich online vor der Reise zu bewerben. Dies ist die effizienteste Option:</p>
<ul>
  <li><strong>Bewerbungswebsite:</strong> visa.immigration.go.tz</li>
  <li><strong>Bearbeitungszeit:</strong> 3–10 Werktage (mindestens 2 Wochen vor der Reise beantragen)</li>
  <li><strong>Kosten:</strong> 50 USD (Einzeleinreise, 90 Tage) oder 100 USD (Mehrfacheinreise)</li>
  <li><strong>Anforderungen:</strong> Reisepass-Scan, Passfoto, Rückflugdetails, Unterkunftsnachweis</li>
</ul>

<h3>Visum bei der Einreise</h3>
<p>Visa sind am Kilimandscharo International Airport (JRO), Julius Nyerere Airport (DAR) und an den wichtigsten Landgrenzen erhältlich:</p>
<ul>
  <li><strong>Kosten:</strong> 50 USD (Einzeleinreise) – US-Bürger zahlen 100 USD</li>
  <li><strong>Zahlung:</strong> Nur Barzahlung in USD (saubere, unbeschädigte Scheine aus dem Jahr 2006 oder später)</li>
  <li><strong>Wartezeit:</strong> 30–60 Minuten je nach Warteschlange</li>
</ul>
<p>Wir empfehlen das E-Visum, um mögliche Warteschlangen zu vermeiden und sicherzustellen, dass Ihr Visum bestätigt ist, bevor Sie Ihren Flug antreten.</p>

<h3>Reisepassanforderungen</h3>
<ul>
  <li>Mindestens 6 Monate über Ihr Einreisedatum hinaus gültig</li>
  <li>Mindestens 2 leere Seiten für Stempel</li>
  <li>Guter Zustand (keine erheblichen Beschädigungen oder Wasserschäden)</li>
</ul>

<h2>Gelbfieberimpfung</h2>
<p>Tansania verlangt einen Gelbfieberimpfnachweis, wenn Sie aus einem Gelbfieberendemiegebiet kommen oder durch ein solches transit gereist sind. Dies betrifft viele Länder in Subsahara-Afrika und Südamerika.</p>
<p><strong>Wichtige Punkte:</strong></p>
<ul>
  <li>Bei direktem Flug aus Europa, Nordamerika, Asien oder dem Nahen Osten ist die Gelbfieberimpfung <strong>nicht verpflichtend</strong>, aber empfohlen</li>
  <li>Bei Transit durch Äthiopien, Kenia oder ein anderes Endemiegebiet <strong>müssen</strong> Sie ein gültiges Gelbfieberzertifikat besitzen</li>
  <li>Die Impfung muss mindestens 10 Tage vor der Ankunft verabreicht worden sein</li>
  <li>Der Internationale Impfschein (gelbe Karte) ist lebenslang gültig</li>
  <li>Sansibar hat eigene Einreiseanforderungen und überprüft in der Regel Gelbfieberzertifikate, auch bei Reisenden, die vom tansanischen Festland ankommen</li>
</ul>

