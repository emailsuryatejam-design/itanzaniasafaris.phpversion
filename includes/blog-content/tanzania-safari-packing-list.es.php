<p>Preparar el equipaje para un safari en Tanzania requiere más reflexión que para unas vacaciones convencionales. En un mismo día puede encontrarse con polvorientas pistas de bush, un sol ecuatorial abrasador, frescas mañanas en las tierras altas y posibles lluvias tropicales. Esta lista de equipaje, elaborada por expertos, le garantiza que llevará todo lo necesario sin cargar con más peso del imprescindible para su aventura en África oriental.</p>

<h2>Ropa Esencial: Qué Ponerse en el Safari</h2>
<p>La ropa de safari debe ser de colores neutros, ligera, transpirable y de secado rápido. Evite los blancos luminosos (atraen las moscas tsetsé y se ensucian rápidamente) y el azul marino oscuro o el negro (absorben el calor y también atraen las moscas tsetsé).</p>
<p><strong>Ropa recomendada:</strong></p>
<ul>
  <li><strong>3-4 camisetas o camisas ligeras</strong> en tonos caqui, oliva, beis o verde salvia. Las mangas largas ofrecen protección solar e impiden las picaduras de insectos.</li>
  <li><strong>2-3 pantalones de safari</strong> — los convertibles con cremallera son ideales por su versatilidad</li>
  <li><strong>1 forro polar o chaqueta abrigada</strong> — las mañanas en el borde del Cráter del Ngorongoro (2.235 m de altitud) pueden ser realmente frescas, hasta 5°C</li>
  <li><strong>1 chubasquero</strong> — ligero y compactable, imprescindible durante la temporada verde</li>
  <li><strong>1 sombrero de ala ancha</strong> — fundamental para protegerse del sol durante los recorridos con el techo abierto</li>
  <li><strong>Calzado cómodo de caminar</strong> — cerrado, ya rodado, para los paseos por la naturaleza y el entorno del campamento</li>
  <li><strong>Sandalias o chanclas</strong> — para relajarse en el lodge o campamento</li>
  <li><strong>Calcetines abrigados y ropa de dormir</strong> — los campamentos en tierras altas pueden ser fríos por la noche</li>
  <li><strong>Bañador</strong> — muchos lodges disponen de piscina</li>
</ul>
<p><strong>Importante:</strong> Los límites de equipaje en los vuelos domésticos de Tanzania son habitualmente de 15-20 kg en bolsas blandas (sin maletas rígidas en aviones pequeños). Viaje ligero y práctico.</p>

<h2>Equipo Fotográfico</h2>
<p>Tanzania ofrece algunas de las mejores oportunidades de fotografía de naturaleza del mundo. Esto es lo que recomendamos:</p>
<ul>
  <li><strong>Cuerpo de cámara:</strong> Réflex o sin espejo con buen rendimiento de autoenfoque</li>
  <li><strong>Objetivo teleobjetivo zoom:</strong> 100-400 mm o 200-600 mm es el rango ideal para la fauna</li>
  <li><strong>Objetivo gran angular:</strong> Para paisajes, fotografía nocturna y tomas del campamento o lodge</li>
  <li><strong>Baterías de repuesto:</strong> Lleve al menos 2-3 de más. La carga puede ser limitada en los campamentos remotos.</li>
  <li><strong>Tarjetas de memoria:</strong> Se recomiendan 128 GB o más. Hará miles de fotos.</li>
  <li><strong>Saco de arena o soporte para ventanilla:</strong> Para estabilizar el objetivo sobre el techo o el marco de la ventanilla del vehículo</li>
  <li><strong>Protección contra el polvo:</strong> Bolsas de plástico, paños para el objetivo y un soplador para limpiar el sensor</li>
  <li><strong>Prismáticos:</strong> Los 8x42 o 10x42 son ideales para avistar fauna lejana y aves</li>
</ul>
<p>Aunque vaya a fotografiar con el teléfono móvil, los modelos actuales con zoom pueden capturar imágenes de safari excelentes. Un adaptador para trípode de bolsillo y unos prismáticos compactos mejorarán notablemente sus resultados.</p>

<h2>Salud y Seguridad</h2>
<ul>
  <li><strong>Medicamentos con receta</strong> — lleve más de lo necesario en caso de retrasos</li>
  <li><strong>Medicación antipalúdica</strong> — consulte a su médico al menos 4-6 semanas antes del viaje. Consulte nuestra <a href="blog-post.php?slug=tanzania-visa-requirements-vaccinations">guía de salud para Tanzania</a>.</li>
  <li><strong>Repelente de insectos</strong> — con DEET al 30% o más, o base de icaridina. Imprescindible al anochecer.</li>
  <li><strong>Protección solar</strong> — FPS 50+. El sol ecuatorial es intenso, especialmente a gran altitud.</li>
  <li><strong>Botiquín básico de primeros auxilios</strong> — tiritas, antiséptico, analgésicos, medicación antidiarreica</li>
  <li><strong>Gel hidroalcohólico</strong> — útil en los recorridos cuando no hay agua para lavarse las manos</li>
  <li><strong>Sales de rehidratación</strong> — para el calor y los largos recorridos en vehículo</li>
  <li><strong>Protector labial con FPS</strong> — a menudo olvidado, pero imprescindible</li>
</ul>

<h2>Documentos y Dinero</h2>
<ul>
  <li><strong>Pasaporte</strong> — válido al menos 6 meses más allá de la fecha de salida, con al menos 2 páginas en blanco para sellos</li>
  <li><strong>Visado para Tanzania</strong> — solicítelo en línea mediante e-visa u obténgalo a la llegada (50 USD entrada simple)</li>
  <li><strong>Certificado de fiebre amarilla</strong> — obligatorio si llega desde un país endémico</li>
  <li><strong>Documentos del seguro de viaje</strong> — con cobertura de evacuación médica (muy recomendable)</li>
  <li><strong>Copias impresas de las reservas</strong> — confirmaciones del hotel, itinerario del safari, billetes de avión</li>
  <li><strong>Dólares estadounidenses</strong> — para propinas, visado a la llegada y compras menores. Lleve billetes impecables posteriores a 2006.</li>
  <li><strong>Tarjeta de crédito o débito</strong> — Visa es la más aceptada en Tanzania. Hay cajeros automáticos en Arusha.</li>
</ul>

<h2>Otros Imprescindibles del Safari</h2>
<ul>
  <li><strong>Linterna frontal o pequeña linterna</strong> — los campamentos y lodges pueden tener iluminación limitada por la noche</li>
  <li><strong>Batería externa (powerbank)</strong> — para cargar dispositivos durante los largos recorridos</li>
  <li><strong>Adaptador de corriente universal</strong> — Tanzania utiliza enchufes tipo G del Reino Unido (3 clavijas)</li>
  <li><strong>Bolsa impermeable o bolsas con cierre hermético</strong> — para proteger los aparatos electrónicos del polvo y la lluvia ocasional</li>
  <li><strong>Botella de agua reutilizable</strong> — la mayoría de los vehículos safari llevan una nevera con agua, pero tener la suya propia es cómodo</li>
  <li><strong>Pequeña mochila de día</strong> — para los recorridos y los paseos, con lo esencial</li>
</ul>

<h2>Consejos de Equipaje Según la Temporada</h2>
<p><strong>Estación seca (junio-octubre):</strong> Condiciones polvorientas — lleve paños extra para el objetivo, un pañuelo o buff para cubrirse la cara y ropa de tonos neutros oscuros que disimule el polvo. Las mañanas pueden ser frescas (10-15°C), especialmente a gran altitud.</p>
<p><strong>Temporada verde (noviembre-mayo):</strong> Lleve un chubasquero de calidad, protección impermeable para la bolsa de la cámara y repelente de mosquitos. Las pistas pueden estar embarradas. La contrapartida: paisajes frondosos de gran belleza fotográfica y cielos dramáticos con tormentas.</p>

