<h2>桑给巴尔最美海滩</h2>

<h3>努古威海滩（北部）</h3>
<p>桑给巴尔岛最负盛名的海滩，位于岛屿最北端。努古威潮差极小，全年均可游泳，日落景色迷人，氛围活跃热烈。豪华度假村、精品旅馆与海滩酒吧各具特色，适合不同风格的旅行者。</p>

<h3>肯德瓦海滩（西北部）</h3>
<p>位于努古威以南，肯德瓦稍显宁静，但同样提供任何潮汐时分均宜游泳的优质海滩。海滩宽阔洁净，几家优质度假村散布其间。肯德瓦礁石度假村举办的著名满月派对吸引全岛各地游客前来狂欢。</p>

<h3>帕杰海滩（东南部）</h3>
<p>东非风筝冲浪之都。帕杰拥有绵长宽阔的海滩，风力稳定、海域浅平，是水上运动爱好者的天堂。相比北部海岸，这里村庄气息更浓，生活节奏更为悠闲随性。</p>

<h3>马泰姆威海滩（东北部）</h3>
<p>更为安静和私密，马泰姆威拥有美丽的珊瑚礁海岸线，是前往姆内姆巴环礁浮潜的出发地点——后者是印度洋西岸最顶级的海洋探索胜地之一。高端精品度假村沿岸而立。</p>

<h3>贾姆比亚尼海滩（东南部）</h3>
<p>桑给巴尔最长的海滩，当地渔村气息浓郁。退潮时可见海藻养殖场景，增添了真实的文化维度。经济型客栈密集，闲适氛围吸引长期住客。</p>

<h2>海滩以外：桑给巴尔精彩活动</h2>

<h3>石头城（联合国教科文组织世界遗产）</h3>
<p>桑给巴尔的历史核心是一座迷宫般的古城，迷蹤小巷、精雕木门、熙攘集市，以及阿拉伯、波斯、印度和欧洲商人留下的数百年层叠历史。主要景点包括奇迹宫、古堡，以及英国圣公会大教堂旧址的前奴隶市场遗址。</p>

<h3>香料种植园参观</h3>
<p>桑给巴尔有"香料岛"之称，种植园游览将带您亲眼见证丁香、香草、肉桂、豆蔻、肉豆蔻和黑胡椒的生长之地。向导讲解每种香料的历史、用途和种植方式，并在途中品尝新鲜热带水果。</p>

<h3>浮潜与潜水</h3>
<p>桑给巴尔及其周边小岛的水域提供世界级海洋体验：</p>
<ul>
  <li><strong>姆内姆巴环礁：</strong>受保护的海洋保护区，海豚、海龟和色彩斑斓的珊瑚随处可见</li>
  <li><strong>丘姆贝岛：</strong>屡获殊荣的海洋公园，拥有印度洋西部保存最完好的珊瑚礁</li>
  <li><strong>图姆巴图岛：</strong>访客较少的潜水点，能见度极佳，海洋生物多样</li>
</ul>

<h3>约扎尼森林</h3>
<p>地球上其他任何地方都找不到的特有物种——桑给巴尔红疣猴在这片珍贵森林中安家。这里还设有红树林木栈道，并栖息着多种鸟类。</p>

