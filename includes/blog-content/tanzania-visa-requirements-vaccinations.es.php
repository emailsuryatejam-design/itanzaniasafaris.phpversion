<h2>Prevención del Paludismo</h2>
<p>El paludismo (malaria) está presente en todo Tanzania, incluidos los destinos de safari y Zanzíbar. La prevención es imprescindible:</p>
<p><strong>Opciones de medicación antipalúdica:</strong></p>
<ul>
  <li><strong>Malarone (Atovacuona/Proguanil):</strong> La opción más popular. Comience 1-2 días antes, continúe 7 días después. Efectos secundarios mínimos.</li>
  <li><strong>Doxiciclina:</strong> Alternativa económica. Comience 1-2 días antes, continúe 28 días después. Puede provocar sensibilidad solar.</li>
  <li><strong>Mefloquina (Lariam):</strong> Dosificación semanal, pero con posibles efectos secundarios neuropsiquiátricos. Consúltelo con su médico.</li>
</ul>
<p><strong>Medidas adicionales de prevención del paludismo:</strong></p>
<ul>
  <li>Aplique repelente de insectos con DEET (30% o más) en la piel expuesta, especialmente al amanecer y al atardecer</li>
  <li>Use manga larga y pantalón largo al anochecer</li>
  <li>Duerma bajo mosquitera (disponible en prácticamente todos los campamentos y lodges de safari)</li>
  <li>Use espirales antimosquitos o enchufes repelentes en su habitación</li>
</ul>

<h2>Seguro de Viaje</h2>
<p>El seguro de viaje completo no es legalmente obligatorio para Tanzania, pero es muy recomendable. Una póliza de calidad debe incluir:</p>
<ul>
  <li><strong>Cobertura de evacuación médica</strong> — Fundamental para zonas de safari alejadas de los hospitales. La evacuación en helicóptero desde el Serengeti a Nairobi puede costar más de 10.000 USD.</li>
  <li><strong>Tratamiento médico de urgencia</strong> — Cobertura de al menos 100.000 USD para hospitalización</li>
  <li><strong>Cancelación/interrupción del viaje</strong> — Protege su inversión si los planes cambian</li>
  <li><strong>Pérdida o retraso de equipaje</strong> — Los vuelos domésticos en aviones pequeños pueden ocasionalmente extraviar el equipaje</li>
  <li><strong>Cobertura en gran altitud</strong> — Si va a <a href="blog-post.php?slug=mount-kilimanjaro-climbing-guide">escalar el Kilimanjaro</a>, asegúrese de que la póliza cubra altitudes de hasta 6.000 m</li>
</ul>

<h2>Otros Consejos de Salud para Tanzania</h2>
<ul>
  <li><strong>Agua:</strong> Beba solo agua embotellada o purificada. Su operador de safari le proporcionará agua potable segura.</li>
  <li><strong>Comida:</strong> Coma en su lodge o campamento, donde se mantienen los estándares de preparación de alimentos. Evite la comida callejera si tiene el estómago sensible.</li>
  <li><strong>Protección solar:</strong> El sol ecuatorial es intenso, especialmente a gran altitud. Protector solar FPS 50+, sombrero y gafas de sol son imprescindibles.</li>
  <li><strong>Altitud:</strong> El borde del Cráter del Ngorongoro está a 2.235 m y el Kilimanjaro alcanza los 5.895 m. Deje tiempo para aclimatarse si viene del nivel del mar.</li>
</ul>

