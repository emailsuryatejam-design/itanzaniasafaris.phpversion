<p>Planificar un safari en Tanzania implica mucho más que elegir los parques y los lodges. Comprender los requisitos de entrada, los procedimientos de visado y los preparativos de salud es esencial para un viaje sin contratiempos. Esta guía cubre todo lo relativo a las solicitudes de visado, las vacunas, la prevención del paludismo y el seguro de viaje, para que pueda llegar a Tanzania completamente preparado.</p>

<h2>Requisitos de Visado para Tanzania</h2>
<p>La mayoría de los visitantes internacionales necesitan visado para entrar en Tanzania. La buena noticia es que el proceso es sencillo y puede completarse en línea o a la llegada.</p>

<h3>E-Visa (Recomendado)</h3>
<p>El sistema de visado electrónico de Tanzania le permite solicitar el visado en línea antes de viajar. Es la opción más eficiente:</p>
<ul>
  <li><strong>Sitio web de solicitud:</strong> visa.immigration.go.tz</li>
  <li><strong>Tiempo de tramitación:</strong> 3-10 días hábiles (solicítelo al menos 2 semanas antes del viaje)</li>
  <li><strong>Coste:</strong> 50 USD (entrada simple, 90 días) o 100 USD (entradas múltiples)</li>
  <li><strong>Requisitos:</strong> Escaneado del pasaporte, foto de pasaporte, detalles del vuelo de regreso, justificante de alojamiento</li>
</ul>

<h3>Visado a la Llegada</h3>
<p>Los visados están disponibles en el Aeropuerto Internacional de Kilimanjaro (JRO), el Aeropuerto Julius Nyerere (DAR) y las principales fronteras terrestres:</p>
<ul>
  <li><strong>Coste:</strong> 50 USD (entrada simple) — los ciudadanos estadounidenses pagan 100 USD</li>
  <li><strong>Pago:</strong> Solo en efectivo en dólares estadounidenses (billetes en perfecto estado, fechados en 2006 o posterior)</li>
  <li><strong>Tiempo de espera:</strong> 30-60 minutos según la cola</li>
</ul>
<p>Recomendamos la e-visa para evitar posibles colas y asegurarse de que el visado esté confirmado antes de embarcar.</p>

<h3>Requisitos del Pasaporte</h3>
<ul>
  <li>Válido durante al menos 6 meses más allá de su fecha de entrada</li>
  <li>Al menos 2 páginas en blanco para los sellos</li>
  <li>En buen estado (sin daños significativos ni marcas de humedad)</li>
</ul>

<h2>Vacuna contra la Fiebre Amarilla</h2>
<p>Tanzania exige la acreditación de la vacuna contra la fiebre amarilla si llega desde o ha transitado por un país endémico. Esto incluye muchos países del África subsahariana y de América del Sur.</p>
<p><strong>Puntos clave:</strong></p>
<ul>
  <li>Si vuela directamente desde Europa, América del Norte, Asia o Oriente Medio, la vacuna contra la fiebre amarilla <strong>no es obligatoria</strong>, pero sí recomendable</li>
  <li>Si hace escala en Etiopía, Kenia u otro país endémico, <strong>debe</strong> tener el certificado de fiebre amarilla en vigor</li>
  <li>La vacuna debe administrarse al menos 10 días antes de la llegada</li>
  <li>El Certificado Internacional de Vacunación (tarjeta amarilla) tiene validez de por vida</li>
  <li>Zanzíbar tiene sus propios requisitos de entrada y habitualmente comprueba los certificados de fiebre amarilla incluso a los viajeros que llegan desde la Tanzania continental</li>
</ul>

