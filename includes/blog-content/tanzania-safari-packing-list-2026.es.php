<h2>Electrónica y Adaptadores</h2>
<ul>
  <li><strong>Adaptador de tipo G (3 clavijas cuadradas)</strong> — Tanzania usa los enchufes del estándar británico. Indispensable.</li>
  <li><strong>Voltaje:</strong> 230V / 50Hz — compatible con la mayoría de los equipos electrónicos actuales (verifique en el etiquetado del adaptador de su dispositivo)</li>
  <li><strong>Batería externa (powerbank) de alta capacidad</strong> — 20.000 mAh o más para cargar cámara, teléfono y otros dispositivos durante los recorridos</li>
  <li><strong>Cargador múltiple</strong> con al menos 3 puertos USB para cargar varios dispositivos simultáneamente</li>
  <li><strong>Tarjeta SIM local</strong> — las tarjetas de Vodacom Tanzania son las más fiables. Cómprela en el aeropuerto o en Arusha. Útil para comunicarse y tener datos.</li>
</ul>

<h2>Consejos de Equipaje por Temporada</h2>
<p><strong>Temporada seca (junio-octubre):</strong> Maximice la protección contra el polvo. Lleve bufanda o buff para cubrirse nariz y boca en los recorridos. Traiga ropa extra de repuesto ya que el polvo lo impregna todo. Las noches en Ngorongoro y en el norte del Serengeti pueden ser genuinamente frías (5-12°C) — no subestime la necesidad de ropa abrigada.</p>
<p><strong>Temporada verde (noviembre-mayo):</strong> El chubasquero es imprescindible. Lleve protección impermeable adicional para la cámara. Los mosquitos son más activos — extreme las precauciones con el repelente. Las pistas pueden estar resbaladizas, por lo que el calzado con buena suela es más importante.</p>

