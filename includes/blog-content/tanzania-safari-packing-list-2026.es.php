<p>Preparar el equipaje para un safari en Tanzania en 2026 requiere pensar en múltiples entornos: polvo del bush durante el día, frío en las tierras altas al amanecer, calor ecuatorial al mediodía y posibles lluvias en la temporada verde. Esta lista actualizada cubre todo lo que necesita llevar, desde la ropa ideal hasta el equipo fotográfico, la medicación y los documentos, con los límites de peso actuales de los vuelos domésticos y las últimas recomendaciones de salud.</p>

<h2>Límites de Peso para Vuelos Domésticos en Tanzania</h2>
<p>Antes de empacar, conozca las restricciones críticas de los vuelos domésticos en Tanzania, que afectan a la mayoría de los safaris por el Circuito Norte:</p>
<ul>
  <li><strong>Peso total del equipaje:</strong> 15-20 kg en la mayoría de las aerolíneas domésticas (Coastal Aviation, Auric Air, Air Excel)</li>
  <li><strong>Tipo de bolsa:</strong> Solo bolsas blandas — sin maletas rígidas. Las bolsas de lona o tipo duffel son las más adecuadas.</li>
  <li><strong>Equipaje de mano:</strong> Generalmente 5 kg adicionales en una mochila pequeña</li>
  <li><strong>Exceso de equipaje:</strong> Posible pero costoso — cobrado por kilo. Evítelo planeando bien.</li>
</ul>
<p>Si llega con más maletas de las que permiten los vuelos domésticos, la mayoría de los lodges en Arusha ofrecen consigna gratuita para el equipaje sobrante durante su safari.</p>

<h2>Ropa: Colores de la Tierra y Sistema de Capas</h2>
<p>La regla de oro del safari es: colores neutros y capas. Los colores de la tierra — caqui, beige, marrón, verde oliva, gris — son la norma por razones prácticas: no alertan a los animales, no atraen insectos como la mosca tsetsé y disimilan el polvo inevitable de las pistas del bush.</p>

<h3>Ropa Esencial para el Safari</h3>
<ul>
  <li><strong>3-4 camisetas de manga larga</strong> en colores neutros — la manga larga protege del sol y los insectos durante los recorridos</li>
  <li><strong>2-3 pantalones convertibles con cremallera</strong> — se convierten en pantalón corto para el mediodía en el campamento</li>
  <li><strong>1 forro polar o chaqueta de forro polar</strong> — imprescindible para los recorridos matutinos al amanecer. En Ngorongoro puede hacer hasta 5°C al amanecer.</li>
  <li><strong>1 chubasquero ligero y compactable</strong> — si viaja entre noviembre y mayo</li>
  <li><strong>1 sombrero de ala ancha</strong> — la protección solar más importante que llevará</li>
  <li><strong>1-2 camisetas de manga corta</strong> para el campamento y descansos</li>
  <li><strong>Calcetines abrigados</strong> — los pies fríos en un recorrido al amanecer arruinan la concentración</li>
  <li><strong>Ropa interior de secado rápido</strong></li>
  <li><strong>Calzado cerrado y ya rodado</strong> — los zapatos nuevos causan ampollas en los paseos</li>
  <li><strong>Sandalias cómodas</strong> para el lodge o campamento</li>
  <li><strong>Bañador</strong> — muchos lodges tienen piscina</li>
</ul>

<h3>Lo Que No Debe Llevar</h3>
<ul>
  <li>Ropa blanca o de color muy claro (se ensucia y atrae insectos)</li>
  <li>Ropa azul marino oscuro o negra (absorbe calor excesivo y atrae moscas tsetsé)</li>
  <li>Prendas con lentejuelas, adornos brillantes o estampados llamativos</li>
  <li>Ropa de camuflaje militar — está prohibida en Tanzania</li>
</ul>

<h2>Equipo Fotográfico para el Safari 2026</h2>
<p>Tanzania es uno de los mejores destinos del mundo para la fotografía de naturaleza. Estos son los equipos recomendados para diferentes niveles:</p>

<h3>Para Fotógrafos Avanzados</h3>
<ul>
  <li><strong>Cuerpo mirrorless o réflex de gama alta</strong> con buen rendimiento ISO (Sony A9III, Nikon Z9, Canon R3 o similares)</li>
  <li><strong>Teleobjetivo 200-600 mm f/5.6</strong> o equivalente — imprescindible para fauna a distancia</li>
  <li><strong>Zoom estándar 24-105 mm</strong> para paisajes y fotos de campamento</li>
  <li><strong>3-4 baterías por cuerpo</strong> — la carga eléctrica en los campamentos remotos puede ser intermitente</li>
  <li><strong>Tarjetas de memoria de 128-256 GB</strong> (al menos 4-6 en total)</li>
  <li><strong>Saco de arena o soporte de ventanilla</strong> para el objetivo sobre el techo del vehículo</li>
</ul>

<h3>Para Fotógrafos Aficionados</h3>
<ul>
  <li><strong>Cámara mirrorless de gama media</strong> (Sony A7C, Fuji X-T5, etc.) con teleobjetivo compacto 100-400 mm</li>
  <li><strong>O un teléfono con cámara de buena calidad</strong> — los iPhone 16 Pro y Pixel 9 Pro capturan imágenes safari sorprendentes</li>
  <li><strong>Adaptador de teleobjetivo para teléfono</strong> si solo lleva smartphone</li>
  <li><strong>Mini trípode flexible</strong> para estabilizar en el vehículo</li>
</ul>

<h3>Protección del Equipo</h3>
<ul>
  <li><strong>Bolsa de cámara con cubierta antipolvo</strong> — el polvo del Serengeti es implacable</li>
  <li><strong>Paños de microfibra</strong> para limpiar lentes varias veces al día</li>
  <li><strong>Soplador de sensor</strong> para eliminar partículas del sensor</li>
  <li><strong>Bolsas con cierre hermético</strong> para proteger la cámara durante los traslados en pista</li>
</ul>

<h2>Medicamentos y Salud: Protocolos 2026</h2>

<h3>Profilaxis Antipalúdica (Obligatoria)</h3>
<p>El paludismo está presente en todas las zonas de safari en Tanzania. Las opciones de medicación más actuales para 2026 son:</p>
<ul>
  <li><strong>Malarone (Atovacuona/Proguanil):</strong> Tratamiento de elección. Comience 1-2 días antes del viaje, continúe 7 días después. Efectos secundarios mínimos. Precio más alto.</li>
  <li><strong>Doxiciclina 100 mg:</strong> Alternativa económica. Comience 1-2 días antes, continúe 28 días después. Puede provocar fotosensibilidad — use protección solar extra.</li>
</ul>
<p>Consulte a su médico al menos 4-6 semanas antes del viaje para elegir la opción correcta según su historial médico.</p>

<h3>Medicamentos Recomendados en el Botiquín</h3>
<ul>
  <li>Repelente con DEET al 30-50% o icaridina — aplíquelo al atardecer</li>
  <li>Protector solar FPS 50+ con rápida reaplicación durante los recorridos</li>
  <li>Antihistamínico para reacciones a picaduras</li>
  <li>Ibuprofeno / paracetamol</li>
  <li>Antidiarreico (loperamida)</li>
  <li>Sales de rehidratación oral — esenciales para el calor</li>
  <li>Antiséptico y tiritas</li>
  <li>Tratamiento ocular por si entra polvo</li>
  <li>Sus medicamentos habituales con receta + dosis extra de reserva</li>
</ul>

