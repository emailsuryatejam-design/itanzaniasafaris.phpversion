<p>当大多数游客蜂拥至塔朗吉雷、塞伦盖蒂和恩戈罗恩戈罗组成的北部明星线路时，坦桑尼亚南部的广袤荒野几乎静候着少数真正懂得欣赏它的旅行者。尼亚里雷（前塞卢斯野生动物保护区）和鲁阿哈国家公园——这两处非洲最壮阔的保护区，为那些渴望突破寻常游猎体验、拥抱真正原始荒野的游客，提供了全非洲最纯粹、最未经雕琢的游猎体验。</p>

<h2>南部线路概况</h2>
<p>坦桑尼亚南部游猎线路，又称"南部线路"（Southern Circuit），主要由两大核心区域组成：</p>
<ul>
  <li><strong>尼亚里雷国家公园（Nyerere National Park）：</strong>前身为塞卢斯野生动物保护区，2022年更名以纪念坦桑尼亚独立运动领袖朱利叶斯·尼雷尔。总面积约30,000平方公里，是非洲最大的单一保护区之一，也是整个非洲最大的野生动物保护区之一。</li>
  <li><strong>鲁阿哈国家公园（Ruaha National Park）：</strong>面积约22,000平方公里，是坦桑尼亚面积最大的国家公园。与尼亚里雷保护区合并计算，整个鲁阿哈-尼亚里雷生态系统覆盖超过50,000平方公里，构成非洲现存最大的保护景观之一。</li>
</ul>
<p>南部线路的最大特点：极少的游客、极丰富的野生动物，以及北部线路无法提供的独特游猎体验——船上游猎。</p>

<h2>尼亚里雷（前塞卢斯）：水道与荒野</h2>

<h3>独特的船上游猎</h3>
<p>尼亚里雷最令人难忘的独特体验，是在鲁菲基河（Rufiji River）及其支流和湖泊网络上进行的船上游猎（Boat Safari）。乘坐小型游艇在河道中穿行，从水面视角观察动物，体验感与陆地游猎驾车截然不同：</p>
<ul>
  <li>从水面近距离观察河马群体在水中互动</li>
  <li>巨型尼罗河鳄在沙滩上晒太阳</li>
  <li>各种水鸟和鸣鸟在河岸林中出没</li>
  <li>大象从河中汲水、在岸边觅食</li>
  <li>夜间船上游猎（部分营地提供）：用探照灯照射，发现河马、鳄鱼和夜行动物</li>
</ul>
<p>鲁菲基河是东非最重要的水道之一，沿岸的植被和野生动物多样性极为丰富。步行游猎也是尼亚里雷的亮点活动之一——由武装护林员陪同在丛林中徒步，是亲近大自然最直接的方式。</p>

<h3>非洲野狗：南部线路的明星物种</h3>
<p>尼亚里雷拥有全非洲最大的非洲野狗种群之一。非洲野狗（African Wild Dog / Painted Dog）是地球上最濒危的大型哺乳动物之一，目前全球存量仅约6,000只。在北部线路极为罕见，但在尼亚里雷的目击率相对较高，追踪一个野狗家族的捕猎行动是游猎爱好者梦寐以求的体验。</p>

<h3>尼亚里雷的其他野生动物</h3>
<ul>
  <li><strong>大象：</strong>数量众多，常见大群大象在鲁菲基河边饮水</li>
  <li><strong>狮子：</strong>鲁菲基地区的狮群密度较高，经常可见成群的狮子</li>
  <li><strong>水牛：</strong>大群水牛在草原上觅食</li>
  <li><strong>河马：</strong>鲁菲基河及各湖泊中河马数量极多</li>
  <li><strong>黑犀牛：</strong>尼亚里雷历史上是黑犀牛的重要栖息地，尽管偷猎在过去造成了损失，但保护工作仍在持续</li>
  <li><strong>鸟类：</strong>超过440种，包括多种稀有猛禽和水鸟</li>
</ul>

<h2>鲁阿哈国家公园：大象的王国</h2>
<p>鲁阿哈以其非凡的大象种群而著称——据估计公园内有10,000头以上的大象，是非洲大象密度最高的保护区之一。这里的大象不仅数量多，而且性格相对温顺（由于长期的保护，已不像某些地区的大象那样受惊逃窜），允许游猎车相对近距离观察，是难得的大象近距离摄影圣地。</p>

<h3>大巴奥巴布树和壮观地貌</h3>
<p>鲁阿哈的地貌以大鲁阿哈河（Great Ruaha River）沿岸的河岸林为核心，向外延伸为荒凉壮阔的灌木丛和梨树高原。公园内分布着坦桑尼亚最高密度的猴面包树，与大象的结合构成了独特的野性美学。</p>

<h3>鲁阿哈的捕食动物</h3>
<ul>
  <li><strong>狮子：</strong>鲁阿哈以庞大的狮群闻名，据估计公园内有超过500头狮子</li>
  <li><strong>豹子：</strong>河岸林中常有目击记录</li>
  <li><strong>猎豹：</strong>鲁阿哈的开阔草原是猎豹的理想栖息地</li>
  <li><strong>非洲野狗：</strong>鲁阿哈-尼亚里雷生态系统整体拥有全非洲最大的野狗种群</li>
  <li><strong>鬣狗：</strong>斑点鬣狗和棕鬣狗均有分布</li>
</ul>

<h2>如何抵达南部线路</h2>
<p>南部线路与北部不同，主要通过达累斯萨拉姆进入：</p>
<ul>
  <li><strong>抵达尼亚里雷：</strong>从达累斯萨拉姆驱车约5至6小时，或乘小型飞机飞行约1小时（有每日航班）</li>
  <li><strong>抵达鲁阿哈：</strong>从达累斯萨拉姆飞往鲁阿哈国家公园约1.5至2小时；或从鲁阿哈乘小型飞机飞往尼亚里雷，用时约1小时，可将两个公园结合</li>
  <li><strong>从阿鲁沙组合：</strong>可将北部线路（塞伦盖蒂）与南部线路（鲁阿哈）组合，通过达累斯萨拉姆中转</li>
</ul>

<h2>南部线路最佳游览时间</h2>
<ul>
  <li><strong>6月至10月（旱季）：</strong>最佳时段。水源减少，野生动物聚集于大鲁阿哈河沿岸，游猎收获最为丰富。尼亚里雷河道系统水位适中，船上游猎最为理想。</li>
  <li><strong>7月至9月：</strong>上述旱季中的最佳月份，野生动物密度最高，天气最为稳定</li>
  <li><strong>11月至5月（绿季）：</strong>植被茂密，许多营地关闭，道路泥泞；部分候鸟只在此期间出现，但总体不建议首次游客选择</li>
</ul>

<h2>南部线路的住宿</h2>
<p>由于游客相对较少，南部线路的住宿总体规模较北部小，但精品程度极高：</p>
<ul>
  <li><strong>Sand Rivers Selous：</strong>尼亚里雷最具代表性的豪华营地之一，直接建于鲁菲基河畔，提供最身临其境的船上游猎体验</li>
  <li><strong>Roho ya Selous：</strong>精品豪华营地，强调可持续性和与自然的融合</li>
  <li><strong>Jongomero Camp（鲁阿哈）：</strong>仅8顶帐篷的极致私密营地，将野狗和大象目击率极高的地点占据</li>
  <li><strong>Kwihala Camp（鲁阿哈）：</strong>紧邻大鲁阿哈河，步行游猎和夜间游猎均可安排</li>
</ul>

