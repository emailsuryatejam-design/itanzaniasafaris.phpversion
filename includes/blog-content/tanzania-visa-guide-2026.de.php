  <li>Ggf. Gelbfieberimpfung beim Tropenmediziner holen</li>
  <li>Reiseversicherung mit Medevac-Deckung abschließen</li>
  <li>USD in sauberen, neuen Scheinen für eventuelle Barzahlungen mitführen</li>
  <li>Alle Dokumente ausdrucken UND digital speichern</li>
</ol>

