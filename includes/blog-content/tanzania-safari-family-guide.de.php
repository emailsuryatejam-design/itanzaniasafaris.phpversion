<h2>Gesundheit und Sicherheit für Familien</h2>
<p>Die Gesundheitsplanung ist bei Familien mit Kindern besonders wichtig:</p>

<h3>Malariaprophylaxe für Kinder</h3>
<ul>
  <li>Malarone ist in Kinderdosen verfügbar und wird von Ärzten häufig für Kinder empfohlen</li>
  <li>Konsultieren Sie einen Kinderarzt oder Reisemediziner <strong>mindestens 6 Wochen</strong> vor der Reise</li>
  <li>Doxycyclin ist <strong>nicht geeignet</strong> für Kinder unter 8 Jahren</li>
  <li>DEET-Repellent in kindgerechter Konzentration (10–30 % DEET) verwenden</li>
</ul>

<h3>Impfungen für Kinder</h3>
<ul>
  <li>Alle Routineimpfungen sollten auf aktuellem Stand sein</li>
  <li>Hepatitis A und Typhus werden für alle Kinder empfohlen</li>
  <li>Gelbfieber: Nur für Kinder ab 9 Monaten; früher Arztbesuch erforderlich</li>
</ul>

<h3>Allgemeine Sicherheitstipps</h3>
<ul>
  <li>Kinder nie unbeaufsichtigt außerhalb der Lodge lassen – wilde Tiere können überall sein</li>
  <li>Sonnenschutz rigoros anwenden – Kinder verbrennen schneller als Erwachsene</li>
  <li>Ausreichend Wasser trinken – Kinder dehydrieren bei Hitze schneller</li>
  <li>Bewegungsschranken während Pirschfahrten respektieren – kein Herauslehn aus Fahrzeugen</li>
</ul>

<h2>Beste Monate für eine Familiensafari in Tansania</h2>
<p>Für Familien empfehlen wir besonders:</p>
<ul>
  <li><strong>Juni–Juli:</strong> Beginn der Trockenzeit, angenehme Temperaturen, gute Wildtierbeobachtung. Nicht so heiß wie August–September. Europäische Schulferien (Sommerferien) fallen günstig zusammen.</li>
  <li><strong>Dezember–Januar:</strong> Weihnachts- und Neujahrsferien. Kalbungszeit beginnt im Januar – Jungtiere faszinieren Kinder besonders. Wärmere Temperaturen, aber angenehm.</li>
  <li><strong>Juli–August:</strong> Hochsaison, aber das beste Wildtiererlebnis. Früh buchen wegen Hochbetrieb.</li>
</ul>
<p>Vermeiden Sie April–Mai (Regenzeit, schlechte Straßen) und die extremen Monate Oktober–November für Familien mit kleinen Kindern.</p>

