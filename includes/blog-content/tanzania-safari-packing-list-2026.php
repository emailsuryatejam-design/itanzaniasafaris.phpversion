<p>Packing correctly for a Tanzania safari is more consequential than packing for most trips. The right clothing keeps you camouflaged and comfortable; the wrong camera equipment means missed shots; forgotten medications can disrupt your trip entirely. This expert packing guide covers every category in practical detail, with specific advice for Tanzania's varying climates, the weight restrictions of bush aircraft, and the practical realities of life in the East African wilderness.</p>

<h2>The Golden Rule: Soft Bags Only</h2>

<p>Before listing anything else, the most important packing rule for Tanzania safari: <strong>use soft-sided luggage only</strong>. Tanzania's internal safari routes rely heavily on small bush aircraft (Cessna Caravans, Cessna 206s, and similar 6–12-seat planes) that have strict baggage policies. Hard-sided suitcases are not permitted on most bush flights because they cannot be compressed to fit the aircraft's luggage compartment.</p>

<ul>
  <li><strong>Weight limit:</strong> Typically 15kg (33lbs) total including hand luggage on bush flights</li>
  <li><strong>Bag type:</strong> Soft duffel bag, roll-top dry bag, or collapsible duffel</li>
  <li><strong>Size:</strong> Bags must lie flat — usually maximum 25cm x 35cm x 55cm</li>
  <li><strong>Solution for international travellers:</strong> Store your hard suitcase at your Arusha hotel and transfer safari essentials into a soft duffel for the internal portion of your trip</li>
</ul>

<h2>Clothing: What to Wear on Safari</h2>

<h3>The Critical Color Rule</h3>
<p>Safari clothing color matters significantly. The principle: <strong>neutral, earth-tone colors only</strong>. You want to blend into the landscape, not stand out against it. Recommended colors:</p>
<ul>
  <li>Khaki, tan, beige</li>
  <li>Olive green, sage, light green</li>
  <li>Light brown, stone, sand</li>
  <li>Grey (acceptable)</li>
</ul>

<p><strong>Colors to avoid entirely:</strong></p>
<ul>
  <li><strong>White:</strong> Highly visible to wildlife and shows dust immediately — Tanzania's dirt tracks coat everything in ochre dust within minutes</li>
  <li><strong>Black:</strong> Attracts tsetse flies, which are common in some parks (particularly Tarangire and Nyerere). Black, dark blue, and dark colors attract these biting insects more than pale ones.</li>
  <li><strong>Bright blue:</strong> Also attracts tsetse flies</li>
  <li><strong>Any bright or neon colors:</strong> Disruptive to wildlife and other guests</li>
  <li><strong>Camouflage patterns:</strong> Military camouflage is illegal in Tanzania and can cause problems at checkpoints</li>
</ul>

<h3>The Layer System: Essential for Temperature Variation</h3>
<p>Tanzania's safari areas have significant temperature swings, particularly at altitude. Ngorongoro Crater rim sits at approximately 2,400m and can be genuinely cold (5–10°C) at night and early morning. Serengeti mornings in the dry season (June–August) start at 10–15°C before warming to 28–32°C by mid-afternoon. Layer accordingly:</p>

<h3>Clothing Checklist</h3>
<ul>
  <li>3–4 lightweight long-sleeve safari shirts (moisture-wicking, UPF-rated preferred)</li>
  <li>2–3 short-sleeve shirts for mid-day warmth</li>
  <li>2 pairs of lightweight safari trousers (convertible zip-off trousers are practical)</li>
  <li>1 pair of comfortable shorts for camp</li>
  <li>1 fleece or light down jacket (essential — Ngorongoro and early Serengeti mornings are cold)</li>
  <li>1 lightweight waterproof jacket or poncho (for green season, and Ngorongoro Crater which creates its own weather)</li>
  <li>Warm hat/beanie (for cold morning drives)</li>
  <li>Wide-brimmed sun hat (essential during the day)</li>
  <li>Buff/neck gaiter (for dust on game drives)</li>
  <li>3–4 pairs of lightweight socks</li>
  <li>Underwear/base layers (merino wool if budget allows — naturally antimicrobial, warms and cools)</li>
  <li>1 casual outfit for Arusha/Zanzibar</li>
  <li>Swimsuit (most camps have pools; Zanzibar obviously)</li>
  <li>Lightweight sarong (versatile — beach, mosque visits in Stone Town, etc.)</li>
</ul>

<h2>Footwear</h2>

<ul>
  <li><strong>Closed-toe walking shoes or trail runners:</strong> For walking safaris, cultural visits, and general camp use. Ankle support is beneficial on rocky terrain.</li>
  <li><strong>Sandals or flip-flops:</strong> For camp relaxation and beach (Zanzibar)</li>
  <li><strong>Do not bring:</strong> High heels, dress shoes, or anything impractical for dirt terrain</li>
</ul>

<p>Safari boots are not strictly necessary unless you are planning extensive bush walks or Kilimanjaro. Comfortable trail runners or cross-trainers handle all game drive and standard camp activities perfectly.</p>

<h2>Camera &amp; Photography Equipment</h2>

<p>Tanzania offers some of the finest wildlife photography opportunities in the world. Being adequately equipped can make a dramatic difference to the quality of your images.</p>

<h3>Camera Bodies</h3>
<ul>
  <li>A DSLR or mirrorless camera with fast autofocus is strongly recommended for wildlife photography</li>
  <li>Bring a backup body if you own one — camera repairs are not available in the bush</li>
  <li>Compact cameras and smartphone cameras can produce good results but limit your ability to isolate subjects and capture fast-moving animals</li>
</ul>

<h3>Lenses</h3>
<ul>
  <li><strong>Telephoto (400mm or longer):</strong> The most important lens for wildlife photography. A 100–400mm zoom, 150–600mm, or a 500mm/600mm prime are all excellent choices.</li>
  <li><strong>Mid-range zoom (70–200mm):</strong> Useful for larger groups of animals and environmental portraits</li>
  <li><strong>Wide-angle (16–35mm or 24mm):</strong> For landscape photography, camp life, and the dramatic skies of the Serengeti</li>
</ul>

<h3>Accessories</h3>
<ul>
  <li><strong>Beanbag:</strong> A partially filled beanbag that drapes over the vehicle window ledge is the single most important photography accessory for safari. It provides a stable platform for long telephoto lenses, replacing the need for a tripod. Buy one before you leave or make your own (fill an empty duffel with dried rice/lentils on arrival).</li>
  <li><strong>Extra batteries &amp; charging cables:</strong> Many camps charge items overnight, but remote camps may have limited power hours. Bring at least 3 batteries per camera body.</li>
  <li><strong>Memory cards:</strong> Bring substantially more than you think you need. A single morning in Ndutu during calving season can fill 64GB easily. Multiple 128GB or 256GB cards recommended.</li>
  <li><strong>Sensor cleaning kit:</strong> Tanzania's dust is pervasive — lens and sensor cleaning is essential</li>
  <li><strong>Weather-resistant bag or rain cover for camera gear</strong></li>
</ul>

<h3>Binoculars</h3>
<p>A quality pair of binoculars transforms your safari experience for wildlife you cannot approach closely, and for birding. Recommended specification: 8x42 or 10x42 (magnification x objective lens diameter). Swarovski, Zeiss, and Leica are the gold standard; excellent mid-range options include Vortex, Nikon Monarch, and Celestron. Do not bring cheap 10x50 binoculars — the exit pupil on inferior optics produces terrible low-light performance exactly when you need it most.</p>

<h2>Sun Protection</h2>

<ul>
  <li><strong>Sunscreen SPF 50+:</strong> Tanzania straddles the equator. UV intensity is high, particularly at altitude (Ngorongoro at 2,400m receives significantly more UV than sea level). Reapply every 2 hours on game drives where you're in an open vehicle.</li>
  <li><strong>Zinc oxide stick:</strong> For nose and lips on particularly sunny full-day drives</li>
  <li><strong>UV-blocking sunglasses:</strong> Polarized lenses are excellent for reducing glare on water sources and salt flats</li>
  <li><strong>Lip balm with SPF</strong></li>
</ul>

<h2>Toiletries &amp; Personal Care</h2>

<ul>
  <li>Basic toiletries (most camps provide soap, shampoo, and conditioner, but bring travel sizes for backup)</li>
  <li>Unscented products where possible — strong perfumes can attract insects</li>
  <li>Wet wipes (invaluable for dusty game drives when running water is not immediately available)</li>
  <li>Hand sanitizer</li>
  <li>Insect repellent: <strong>DEET-based repellent (minimum 30% DEET) is essential</strong>. Apply to skin and clothing at dusk and dawn. Picaridin is an effective DEET-free alternative. Do not rely on citronella-based products alone.</li>
  <li>Small travel towel (useful for mobile camps or camping)</li>
  <li>Toilet paper (always keep a roll in your day pack for remote areas)</li>
  <li>Feminine hygiene products (bring your preferred brands — availability in remote areas is unreliable)</li>
</ul>

<h2>Medications &amp; Health Supplies</h2>

<h3>Malaria Prophylaxis</h3>
<p>Malaria prophylaxis is <strong>strongly recommended</strong> for all visitors to Tanzania's safari areas. The main options are:</p>
<ul>
  <li><strong>Malarone (atovaquone/proguanil):</strong> Most commonly prescribed for Tanzania; begin 1–2 days before arrival, continue 7 days after departure; minimal side effects for most people</li>
  <li><strong>Doxycycline:</strong> Inexpensive and effective; must be taken daily; increases sun sensitivity (use high SPF sunscreen diligently); begin 1–2 days before, continue 28 days after</li>
  <li><strong>Lariam (mefloquine):</strong> Weekly tablet; some neurological/psychological side effects in a minority; begin 2–3 weeks before departure</li>
</ul>
<p>Consult a travel medicine physician or tropical medicine clinic at least 6 weeks before travel for personalised advice. Do not self-prescribe based solely on online guides.</p>

<h3>Vaccinations Reminder</h3>
<p>Tanzania's recommended vaccinations include: Yellow Fever (see below), Typhoid, Hepatitis A, Hepatitis B, Rabies (recommended for rural areas and extended stays), Tetanus (routine booster), and Meningococcal (for some travelers). All routine vaccinations should be current.</p>

<h3>First Aid Kit</h3>
<ul>
  <li>Paracetamol and ibuprofen</li>
  <li>Antihistamine (oral — for allergic reactions, insect bites)</li>
  <li>Antihistamine cream (topical — for bite relief)</li>
  <li>Antiseptic wipes and cream (Savlon or Betadine)</li>
  <li>Plasters/band-aids in multiple sizes</li>
  <li>Blister pads (if hiking or Kilimanjaro)</li>
  <li>Tweezers (for thorns and splinters)</li>
  <li>Oral rehydration salts</li>
  <li>Anti-diarrheal medication (Imodium)</li>
  <li>Broad-spectrum antibiotic (discuss with your doctor — ciprofloxacin or azithromycin sometimes recommended for travelers)</li>
  <li>Digital thermometer</li>
  <li>Any personal prescription medications (bring more than needed, in original packaging, with a copy of the prescription)</li>
</ul>

<h2>Documents &amp; Travel Essentials</h2>

<ul>
  <li><strong>Passport:</strong> Valid for at least 6 months beyond your travel dates (Tanzania strictly enforces this). Keep a photocopy stored separately from the original.</li>
  <li><strong>Tanzania e-Visa confirmation:</strong> Print a copy and save a digital copy. See our detailed <a href="blog-post.php?slug=tanzania-visa-requirements-vaccinations">Tanzania Visa Guide 2026</a> for application instructions.</li>
  <li><strong>Yellow Fever Vaccination Certificate (Carte Jaune):</strong> Mandatory if arriving from or transiting through a yellow fever-endemic country. Even if not arriving from an endemic country, carrying this certificate is strongly advisable as individual immigration officers sometimes request it regardless. Zanzibar immigration occasionally asks for it even on internal Tanzania flights.</li>
  <li><strong>Travel insurance documents:</strong> Must include medical evacuation coverage. Emergency medical evacuation from remote Tanzania can cost $15,000–$50,000 — travel without evacuation insurance is not advisable. Flying Doctors / AMREF membership is widely recommended for East Africa.</li>
  <li><strong>Safari itinerary &amp; booking confirmations</strong></li>
  <li><strong>Emergency contacts list</strong></li>
  <li><strong>Credit/debit cards:</strong> Visa and Mastercard accepted in Arusha and major hotels. Carry USD cash for tips, souvenirs, and smaller purchases. Tanzania's tipping culture for safari guides and camp staff is meaningful — budget $15–$25/day for your guide and $5–$10/day for camp staff.</li>
</ul>

<h2>Electronics &amp; Adapters</h2>

<ul>
  <li><strong>Power adapters:</strong> Tanzania uses <strong>Type G plugs</strong> (the same three-square-pin plug as the UK). Voltage is 230V/50Hz. Bring a Type G adapter if your devices use different plugs.</li>
  <li>A universal multi-country adapter is a reliable backup</li>
  <li>Portable power bank (important for remote camps with limited charging hours)</li>
  <li>Lightweight laptop or tablet (if you need to work; most camps have WiFi of varying quality)</li>
  <li>E-reader (long journeys and early nights in camp make reading time plentiful)</li>
  <li>Headlamp or torch with spare batteries (essential — camps frequently have no path lighting)</li>
</ul>

<h2>Packing for Different Seasons</h2>

<h3>Dry Season (June–October)</h3>
<p>Add: warm fleece or down jacket (especially for Ngorongoro), neck buff for dust, extra lens cleaning cloths, lip balm for dry air. The dust in the dry season is significant — your camera gear, clothing, and even food will collect ochre dust. Sealed dry bags for electronics are advisable.</p>

<h3>Wet Season (November–May)</h3>
<p>Add: lightweight waterproof jacket, waterproof bag covers for camera gear, gaiters (if walking), quick-dry clothing. Reduce: heavy dustproofing gear. The wet season also means more insects, so stronger DEET application and attention to mosquito net use becomes more important.</p>

<h2>What NOT to Bring</h2>

<ul>
  <li>Hard-sided suitcases for safari (bush flight restrictions)</li>
  <li>Military camouflage clothing (illegal in Tanzania)</li>
  <li>White, black, or bright-colored clothing (wildlife and practical reasons)</li>
  <li>Excessive jewelry or valuable watches (unnecessary and a theft target)</li>
  <li>Large quantities of electronic devices you won't use</li>
  <li>Drones (illegal in Tanzania's national parks without TANAPA permits, which are rarely granted)</li>
  <li>Excessive footwear (two pairs maximum — trail shoes and sandals)</li>
</ul>

