<p>Tanzanya'nın kuzey parkları dünya kamuoyunda haklı bir yer edinmiş olsa da, gerçek safari tutkunları için asıl gizem güneyde yatar. Nyerere Milli Parkı (eski adıyla Selous) ve Ruaha Milli Parkı, Afrika'nın en büyük ve en az keşfedilmiş koruma alanlarından ikisini oluşturur. Kalabalıktan uzak, el değmemiş vahşi doğanın sunduğu o özgün deneyim için güney Tanzanya safarisi, bilinçli gezginlerin yöneldiği tercih haline gelmektedir.</p>

<h2>Neden Güney Devre Farklı?</h2>
<p>Kuzey Tanzanya'daki Serengeti ve Ngorongoro'nun muhteşem yaban hayatı tartışılmaz. Ancak Güney Devre'nin cazibesi tamamen farklı bir kaynaktan besleniyor: özgünlük, yalnızlık ve keşfetme duygusu. Güney parkların bazılarında günde yalnızca birkaç araçla yüzleşeceğiniz bir yaban hayatı deneyimi yaşanır — bu his, kuzey parkların zirve sezonunda pratikte imkânsızdır.</p>

<h2>Nyerere Milli Parkı: Afrika'nın En Büyük Koruma Alanı</h2>
<p>2019 yılında Tanzanya'nın ilk devlet başkanı Julius Nyerere'nin onuruna yeniden adlandırılan Nyerere Milli Parkı (eski adıyla Selous), 30.000 kilometrekarenin üzerinde koruma alanıyla Afrika'nın — ve dünyanın — en büyük milli parklarından birini oluşturur. Karşılaştırma yapacak olursak: bu alan, İsviçre'nin toplam yüzölçümüne yakındır.</p>

<h3>Benzersiz Safari Deneyimleri</h3>
<p><strong>Tekne Safarisi:</strong> Nyerere, Tanzanya'da nehir ve göl boyunca tekne safarisi yapma imkanı sunan nadir parklar arasındadır. Rufiyi Nehri'nde yüzen timsahlar, suya inen filler, nehir kıyısında dinlenen suaygırları ve ağaçlarda konumlanan kuşlar tekne üzerinden gözlemlenebilir. Bu deneyim, kara araçlarıyla hiçbir zaman elde edilemeyecek yakın mesafeli ve benzersiz perspektifler sunar.</p>
<p><strong>Yürüyüş Safarisi:</strong> Silah taşıyan deneyimli ranger'lar eşliğinde yapılan yürüyüş safarileri, Nyerere'de özellikle iyi organize edilmiştir. Zemin düzeyinde vahşi doğayla kurulan bu bağ, her game drive'ın çok ötesinde bir deneyim sunar.</p>
<p><strong>Fly-Camping:</strong> Bazı operatörler, gökyüzü çadır ile tüm vahşi doğanın tam ortasında geceleme imkanı sunar. Aile çadırlarından uzakta, yalnızca çalılığın seslerine eşlik ederek geçirilen bir gece, unutulmaz bir deneyime dönüşür.</p>

<h3>Yaban Hayatı</h3>
<p>Nyerere, Tanzanya'nın yaban köpekleri için en önemli kalelerinden biridir. Popülasyonları tehlike altında olan bu eşsiz avcıları görmek için Nyerere son derece değerli bir destinasyondur.</p>
<ul>
  <li><strong>Yaban köpekleri (Afrika vahşi köpeği):</strong> Bölgedeki en güçlü popülasyonlardan biri; sabah ve akşam avlanma saatlerinde gözlem şansı yüksektir</li>
  <li><strong>Filler:</strong> Büyük sürüler, özellikle Rufiyi Nehri çevresinde</li>
  <li><strong>Aslanlar ve leoparlar:</strong> Yetersiz ziyaretçi varlığı nedeniyle araç stresinden uzak, son derece doğal davranışlar sergiler</li>
  <li><strong>Timsahlar ve suaygırları:</strong> Nehir boyunca son derece bol</li>
  <li><strong>Kuşlar:</strong> 440+ kayıtlı tür; tekne safarisinde muhteşem gözlem imkanı</li>
</ul>

<h2>Ruaha Milli Parkı: Filler, Vahşilik ve Yalnızlık</h2>
<p>Ruaha, Tanzanya'nın ve Afrika'nın en büyük milli parklarından biridir; 20.000 kilometrekarelik alanıyla Serengeti'den büyüktür. Büyük Rift Vadisi'nin güney ucunda yer alan Ruaha, sarp kayalık tepeler, geniş ova düzlükleri ve mevsimlik nehirler boyunca uzanan sürüklü ağaç toplulukları ile son derece dinamik bir manzara yapısına sahiptir.</p>

<h3>Ruaha'nın 10.000'den Fazla Fili</h3>
<p>Ruaha'nın en çarpıcı istatistiği filleridir: park ve çevre koruma alanları birlikte 10.000'den fazla fil barındırmaktadır. Bu rakam Tanzanya'daki herhangi bir ekosistemin en yüksek değerini temsil ediyor olabilir. Rufiyi Nehri ile Great Ruaha Nehri boyunca, özellikle kurak mevsim ortasında gerçekten etkileyici sürüler görülür.</p>

<h3>Ruaha'nın Aslanları: Sıradışı Bir Popülasyon</h3>
<p>Ruaha, Afrika'nın en yüksek aslan yoğunluklarından birine sahiptir; bazı tahminlere göre 600-700 aslan yaşamaktadır. Daha çarpıcısı, Ruaha aslanlarının olağandışı av davranışları sergilemesidir: bölgedeki aslan sürüleri fil avlamak için iş birliği yapar. Bu nadir davranış belgelenmiş ve bilimsel açıdan önem taşıyan bir gözlemdir.</p>

<h3>Diğer Yaban Hayatı</h3>
<ul>
  <li><strong>Leoparlar:</strong> Ruaha'nın kayalık araziyle örtülü topografyası mükemmel leopar habitatı oluşturur; kayalık alanlarda ve nehir kenarı ağaçlarda görülür</li>
  <li><strong>Sırtlan sürüleri:</strong> Ruaha, Tanzanya'nın en büyük ve en aktif benekli sırtlan topluluklarından bazılarına ev sahipliği yapar</li>
  <li><strong>Kuiebas ve dağ reedbok:</strong> Kuzey parkların düzlüklerine kıyasla daha farklı yaylar ve antilop türleri gözlemlenir</li>
  <li><strong>Geniş kuş listesi:</strong> 570+ tür; Tanzanya'nın en zengin kuş gözleme ortamlarından biri</li>
</ul>

<h2>Güney Devre'ye Nasıl Ulaşılır?</h2>
<p>Güney Tanzanya'nın coğrafi uzaklığı, yalnızlığının temel nedenidir; bu aynı zamanda güzergah planlamanızı daha özenli yapmanızı gerektiriyor.</p>

<h3>Nyerere'ye Ulaşım</h3>
<ul>
  <li><strong>Dar es Salaam'dan charter uçuş:</strong> En yaygın ve pratik yöntem. Uçuş süresi 45-60 dakika; parkın birden fazla airstrip'ine iniş yapılabilir.</li>
  <li><strong>Karayolu:</strong> Dar es Salaam'dan yaklaşık 4-5 saatlik sürüş; yolun bir bölümü kaba toprak yol olabilir</li>
  <li><strong>Tren:</strong> Dar es Salaam ile Zambia arasındaki efsanevi TAZARA hattı, park sınırından geçer — maceracı ruhlar için ilginç bir seçenek</li>
</ul>

<h3>Ruaha'ya Ulaşım</h3>
<ul>
  <li><strong>Dar es Salaam'dan charter uçuş:</strong> Ruaha'ya en hızlı erişim. Msembe ve Jongomero airstrip'lerine iniş yapılabilir.</li>
  <li><strong>Iringa üzerinden karayolu:</strong> Dar es Salaam'dan Iringa'ya yaklaşık 6 saat, oradan Ruaha'ya 2 saat daha. Toprak yol bölümleri mevcuttur.</li>
  <li><strong>Nyerere + Ruaha kombinasyonu:</strong> İki parkı birlikte ziyaret edecekler için Dar es Salaam → Nyerere (uçuş) → Ruaha (uçuş) → Dar es Salaam (uçuş) döngüsü en verimli güzergahtır</li>
</ul>

<h2>En İyi Zaman: Haziran-Ekim</h2>
<p>Her iki güney parkı için de zirve dönem kurak mevsim olan Haziran-Ekim'dir:</p>
<ul>
  <li>Sular çekilir; hayvanlar nehirler boyunca yoğunlaşır</li>
  <li>Bitki örtüsü seyrelerek oyun izleme son derece verimleşir</li>
  <li>Yollar kuru ve erişilebilir</li>
  <li>Yaban köpeği avlanma aktivitesi en üst düzeyde</li>
  <li>Hava: Gündüz ılık (22-28°C), gece serin; giysi katmanlaması önerilir</li>
</ul>
<p>Kasım-Mayıs yağmur mevsiminde bazı kamplar kapanır ve yollar zorlaşır. Bununla birlikte yeşil sezon fiyatları çok daha uygun olup nadiren ziyaret edilen bu dönemde büyüleyici bir manzara deneyimi sunmaktadır.</p>

