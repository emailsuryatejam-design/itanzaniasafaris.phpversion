<h2>迁徙兽群以外的野生动物</h2>
<p>即便不考虑大迁徙兽群，塞伦盖蒂的常驻野生动物同样令人叹为观止：</p>
<ul>
  <li><strong>狮子：</strong>塞伦盖蒂拥有非洲最大的狮群（约3,000头），塞罗内拉岩丘周边的狮群动态举世闻名。</li>
  <li><strong>豹子：</strong>塞罗内拉河谷是非洲大陆豹子目击率最高的地点之一，常可见豹子懒卧于腊肠树枝头。</li>
  <li><strong>猎豹：</strong>开阔的南部平原是猎豹的绝佳栖息地，尤其在猎物丰盛的产犊季节。</li>
  <li><strong>大象：</strong>大象群频繁出没于北部和西部区域，尤其集中在河流附近。</li>
  <li><strong>河马：</strong>中部塞伦盖蒂的雷蒂马河马池是东非河马聚集最密集的地点之一。</li>
  <li><strong>鸟类：</strong>超过500种，从高大的鸵鸟到娇小的太阳鸟。11月至4月有来自欧洲的候鸟到访。</li>
</ul>

<h2>塞伦盖蒂游猎活动</h2>
<p><strong>游猎驾车：</strong>经典的塞伦盖蒂体验。乘坐专业向导驾驶的开顶四驱车，在清晨和下午出发游览。清晨驾车捕食动物活动最为旺盛，光线也最适合摄影。</p>
<p><strong>热气球游猎：</strong>日出时分飘浮于无边草原之上，俯瞰万千生灵——这是任何游猎驾车都无法媲美的视角。全年可从多个起飞点出发，费用约为每人550至600美元，含香槟丛林早餐。</p>
<p><strong>步行游猎：</strong>由武装护林员陪同的徒步探索，提供亲近自然的丛林地面视角，可在特定保护区内进行。</p>
<p><strong>夜间游猎：</strong>部分私人保护区允许夜间驾车，揭开土豚、婴猴和正在捕猎的鬣狗等夜行动物的神秘面纱。</p>

