<h2>所有路线的共同建议</h2>
<ul>
  <li>始终选择比最短天数更长的版本——多一天往往带来截然不同的成功率</li>
  <li>出发前2至3个月开始系统性的体能训练</li>
  <li>与医生讨论是否服用乙酰唑胺（Diamox）预防高原反应</li>
  <li>选择有良好口碑、重视向导培训和背夫福利的运营商</li>
  <li>山上每天饮水量维持在3至4升，帮助身体适应高原</li>
