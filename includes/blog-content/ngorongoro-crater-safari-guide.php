<h2>Ngorongoro Crater Floor: Key Habitats and Locations</h2>
<p>The crater floor covers 260 km² and contains four distinct habitats, each supporting different species:</p>

<h3>1. Short-Grass Plains (Central Crater Floor)</h3>
<p>The open grasslands dominate the central floor. This is lion territory — prides hunt here and rest on termite mounds. You will find wildebeest, zebra, gazelles, and the large buffalo herds that attract predators. Black rhino are most commonly spotted in this area during morning drives.</p>

<h3>2. Lerai Forest (Acacia Forest)</h3>
<p>A dense acacia woodland on the southern floor. This is where elephants feed and where leopards are occasionally spotted lounging in the trees. Vervet monkeys and baboons are always present. This is also a popular lunch spot for safari vehicles — bring a packed lunch and watch out for the resident black kites.</p>

<h3>3. Ngoitokitok Springs (Hippo Pool)</h3>
<p>A freshwater spring on the eastern floor surrounded by swamp vegetation. This is where the crater's 70+ hippos spend their days. Yellow-billed storks, African fish eagles, and kingfishers are always present around the water. The hippo pool is a highlight of any crater game drive.</p>

<h3>4. Lake Magadi (Soda Lake)</h3>
<p>A shallow alkaline lake on the western crater floor. When conditions are right (especially November–April), thousands of flamingos feed on the algae in the shallows, turning the lake pink. Pelicans and other waterbirds are always present.</p>

<h2>Safari Activities at Ngorongoro</h2>
<p><strong>Crater Floor Game Drive (full or half day):</strong> The primary activity. A full-day game drive (6–8 hours) gives you the best chance of seeing all major species. A half-day drive (4–5 hours) is sufficient for most wildlife highlights. All drives require a licensed driver-guide and park-registered vehicle — access to the crater floor is strictly controlled.</p>

<p><strong>Crater Rim Nature Walk:</strong> Guided walks along the forested rim provide dramatic panoramic views of the crater below. You may encounter buffalo, elephant, and highland forest birds (Ngorongoro has 550+ bird species). The rim sits at 2,235m, so layers are essential — it can be 10°C cooler than Arusha.</p>

<p><strong>Olduvai Gorge:</strong> Located 50km west of Ngorongoro on the road to the Serengeti, this is one of the world's most important paleoanthropological sites. Louis and Mary Leakey discovered Homo habilis and Australopithecus boisei remains here in the 1950s–1960s. The small on-site museum and guided gorge walk are excellent and add 1.5–2 hours to your journey to the Serengeti.</p>

<p><strong>Maasai Cultural Visit:</strong> The Ngorongoro Conservation Area is unique in allowing the Maasai to live alongside wildlife. A visit to a traditional Maasai boma (homestead) offers genuine insight into pastoral Maasai life. Communities live on the crater rim and in the surrounding Conservation Area, moving their livestock through the landscape as they have for centuries.</p>

