<h2>A Geological Wonder: The World's Largest Intact Caldera</h2>
<p>Formed when a massive supervolcano collapsed inward roughly 2–3 million years ago, the Ngorongoro Crater is the world's largest unbroken, unflooded volcanic caldera. Its dimensions are staggering:</p>
<ul>
  <li><strong>Diameter:</strong> 19 kilometres (12 miles) across</li>
  <li><strong>Depth:</strong> 610 metres (2,000 feet) from rim to floor</li>
  <li><strong>Floor area:</strong> 260 square kilometres (100 square miles)</li>
  <li><strong>Rim altitude:</strong> 2,235 metres (7,500 feet) above sea level</li>
  <li><strong>Resident wildlife:</strong> approximately 25,000–30,000 large animals</li>
</ul>
<p>The crater walls act as a natural enclosure, trapping moisture from the sky and creating a self-contained ecosystem with multiple distinct habitats: short-grass plains, swamps, highland forests, a soda lake (Lake Magadi), and the Lerai Forest. This habitat diversity is why so many species coexist here in extraordinary density.</p>

<h2>Wildlife in the Crater: Big Five and Beyond</h2>
<p>The Ngorongoro Crater has the highest density of large mammals in Africa — a remarkable consequence of the natural walls keeping prey species (and their predators) within a relatively small, water-rich area. It is one of the few places on Earth where you can realistically see all Big Five in a single game drive.</p>

