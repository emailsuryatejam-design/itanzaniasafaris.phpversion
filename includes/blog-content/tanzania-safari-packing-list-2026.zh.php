<h2>服装：大地色系，分层穿着</h2>
<p>游猎服装的核心原则：中性大地色（卡其色、橄榄绿、棕褐色、沙色、石板灰），轻薄透气，速干易洗，便于分层叠穿。绝对避免：亮白色（容易脏且吸引采采蝇）、深蓝色或黑色（吸热且吸引昆虫）、迷彩图案（在坦桑尼亚部分地区被视为军事服装，可能引起不必要的麻烦）。</p>

<h3>上衣和外套</h3>
<ul>
  <li><strong>3至4件长袖衬衫/T恤：</strong>大地色系，吸湿排汗材质。长袖同时提供防晒和防蚊虫保护，兼顾实用与美观。</li>
  <li><strong>1至2件轻薄短袖T恤：</strong>用于中午气温最高时段或营地休息。</li>
  <li><strong>1件中厚抓绒或保暖夹克：</strong>恩戈罗恩戈罗火山口边缘（海拔2,235米）清晨气温可低至5至8摄氏度，乞力马扎罗地区更低，保暖外套必不可少。</li>
  <li><strong>1件防水冲锋衣：</strong>轻薄可压缩收纳款，绿季（11月至5月）必备，旱季游猎也建议携带以应对突发降雨。</li>
</ul>

