<h2>Packing for a Migration Safari</h2>
<ul>
  <li><strong>Binoculars</strong> — 8x42 or 10x42 are ideal for watching herds at distance and tracking crossing build-ups</li>
  <li><strong>Camera with long zoom lens</strong> — 400mm+ for wildlife; a 100–400mm zoom is the versatile choice</li>
  <li><strong>Neutral colours</strong> — khaki, olive, brown, grey. No white or bright colours that startle animals</li>
  <li><strong>Layers</strong> — early morning game drives in open vehicles are cold; afternoon is hot. Temperatures swing 15°C+ in a single day</li>
  <li><strong>Dust protection</strong> — the dry season crossing period (August–October) generates significant dust. A buff/scarf and sunglasses are essential</li>
  <li><strong>Patience</strong> — the most important item you can bring</li>
</ul>

<h2>Frequently Asked Questions About the Great Migration</h2>

<div class="blog-faq">

  <div class="blog-faq-item">
    <h3>When is the Great Migration in Tanzania?</h3>
    <p>The Great Migration is a year-round event in Tanzania. The wildebeest calving season runs January–March in the southern Serengeti near Ndutu. The Grumeti River crossings happen June–July in the Western Corridor. The famous Mara River crossings — the dramatic peak of the migration — occur August–October in the northern Serengeti (Kogatende area).</p>
  </div>

  <div class="blog-faq-item">
    <h3>Which month is best to see the wildebeest migration?</h3>
    <p>August and September are widely considered the best months. This is when the largest Mara River crossings occur, with herds of 50,000+ animals at once. However, February (calving season — 500,000 calves born) and June–July (Grumeti crossings, fewer crowds) are equally spectacular in different ways.</p>
  </div>

  <div class="blog-faq-item">
    <h3>How long does the Great Migration last?</h3>
    <p>The Great Migration is continuous — it never stops. As a year-round circular movement covering 800km, the wildebeest are always migrating somewhere. The most dramatic phases (river crossings) span approximately 5 months total: Grumeti (June–July) and Mara (August–October).</p>
  </div>

  <div class="blog-faq-item">
    <h3>Where exactly do I go to see the Mara River crossings?</h3>
    <p>Stay in the northern Serengeti, specifically the Kogatende area. Crossing points are concentrated along a 10km stretch of the Mara River in this region. Most camps in the area are positioned within 15–30 minutes' drive of the best crossing points. Access is via the Kogatende or Lamai airstrips.</p>
  </div>

  <div class="blog-faq-item">
    <h3>How much does a Great Migration safari cost?</h3>
    <p>Mid-range migration safaris cost approximately $350–$600 per person per day. Luxury and mobile camps run $800–$2,500+ per day. A 7-night itinerary covering the Mara crossings typically costs $2,500–$12,000 per person including accommodation, meals, game drives, and park fees.</p>
  </div>

  <div class="blog-faq-item">
    <h3>Do the wildebeest cross the river every day?</h3>
    <p>No — crossings are unpredictable. Herds may gather on the riverbank for 3–5 days before crossing, or attempt multiple crossings in a single day. A single animal triggers the herd to follow. Staying 3+ nights near the Mara River is the most effective way to guarantee witnessing a crossing.</p>
  </div>

  <div class="blog-faq-item">
    <h3>Is the Great Migration only in Tanzania?</h3>
    <p>The migration crosses both Tanzania and Kenya. The wildebeest spend roughly 10 months per year in Tanzania's Serengeti and 2 months in Kenya's Masai Mara (August–October). Tanzania offers the complete migration including the calving season, which cannot be seen in Kenya. For the full experience, Tanzania is the better base.</p>
  </div>

