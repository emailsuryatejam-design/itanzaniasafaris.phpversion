<p>Planning a Tanzania safari involves more than choosing parks and lodges. Understanding entry requirements, visa procedures, and health preparations is essential for a smooth trip. This guide covers everything from visa applications to vaccinations, malaria prevention, and travel insurance so you can arrive in Tanzania fully prepared.</p>

<h2>Tanzania Visa Requirements</h2>
<p>Most international visitors require a visa to enter Tanzania. The good news is the process is straightforward and can be completed online or on arrival.</p>

<h3>E-Visa (Recommended)</h3>
<p>Tanzania's electronic visa system allows you to apply online before traveling. This is the most efficient option:</p>
<ul>
  <li><strong>Application website:</strong> visa.immigration.go.tz</li>
  <li><strong>Processing time:</strong> 3-10 business days (apply at least 2 weeks before travel)</li>
  <li><strong>Cost:</strong> $50 USD (single entry, 90 days) or $100 USD (multiple entry)</li>
  <li><strong>Requirements:</strong> Passport scan, passport photo, return flight details, accommodation proof</li>
</ul>

<h3>Visa on Arrival</h3>
<p>Visas are available at Kilimanjaro International Airport (JRO), Julius Nyerere Airport (DAR), and major land borders:</p>
<ul>
  <li><strong>Cost:</strong> $50 USD (single entry) — US citizens pay $100 USD</li>
  <li><strong>Payment:</strong> Cash USD only (crisp, undamaged bills dated 2006 or later)</li>
  <li><strong>Wait time:</strong> 30-60 minutes depending on queue</li>
</ul>
<p>We recommend the e-visa to avoid potential queues and to ensure your visa is confirmed before you board your flight.</p>

<h3>Passport Requirements</h3>
<ul>
  <li>Valid for at least 6 months beyond your date of entry</li>
  <li>At least 2 blank pages for stamps</li>
  <li>Good condition (no significant damage or water damage)</li>
</ul>

<h2>Yellow Fever Vaccination</h2>
<p>Tanzania requires proof of yellow fever vaccination if you are arriving from or have transited through a yellow fever endemic country. This includes many countries in Sub-Saharan Africa and South America.</p>
<p><strong>Key points:</strong></p>
<ul>
  <li>If flying directly from Europe, North America, Asia, or the Middle East, yellow fever vaccination is <strong>not mandatory</strong> but is recommended</li>
  <li>If transiting through Ethiopia, Kenya, or another endemic country, you <strong>must</strong> have a valid yellow fever certificate</li>
  <li>The vaccination must be administered at least 10 days before arrival</li>
  <li>The International Certificate of Vaccination (yellow card) is valid for life</li>
  <li>Zanzibar has its own entry requirements and typically checks yellow fever certificates even for travelers arriving from mainland Tanzania</li>
</ul>

