<h2>Malaria Prevention</h2>
<p>Malaria is present throughout Tanzania, including safari destinations and Zanzibar. Prevention is essential:</p>
<p><strong>Antimalarial medication options:</strong></p>
<ul>
  <li><strong>Malarone (Atovaquone/Proguanil):</strong> Most popular choice. Start 1-2 days before, continue 7 days after. Minimal side effects.</li>
  <li><strong>Doxycycline:</strong> Affordable alternative. Start 1-2 days before, continue 28 days after. Can cause sun sensitivity.</li>
  <li><strong>Mefloquine (Lariam):</strong> Weekly dosing but potential neuropsychiatric side effects. Discuss with your doctor.</li>
</ul>
<p><strong>Additional malaria prevention measures:</strong></p>
<ul>
  <li>Apply DEET-based insect repellent (30%+) to exposed skin, especially at dusk and dawn</li>
  <li>Wear long sleeves and trousers in the evenings</li>
  <li>Sleep under a mosquito net (provided at virtually all safari camps and lodges)</li>
  <li>Use mosquito coils or plug-in repellents in your room</li>
</ul>

<h2>Travel Insurance</h2>
<p>Comprehensive travel insurance is not legally required for Tanzania but is strongly recommended. A quality policy should include:</p>
<ul>
  <li><strong>Medical evacuation coverage</strong> — Critical for safari areas far from hospitals. Helicopter evacuation from the Serengeti to Nairobi can cost $10,000+.</li>
  <li><strong>Emergency medical treatment</strong> — Coverage of at least $100,000 for hospitalization</li>
  <li><strong>Trip cancellation/interruption</strong> — Protects your investment if plans change</li>
  <li><strong>Baggage loss/delay</strong> — Domestic flights in small planes can occasionally misroute luggage</li>
  <li><strong>High altitude coverage</strong> — If <a href="blog-post.php?slug=mount-kilimanjaro-climbing-guide">climbing Kilimanjaro</a>, ensure your policy covers altitude up to 6,000m</li>
</ul>

<h2>Other Health Tips for Tanzania</h2>
<ul>
  <li><strong>Water:</strong> Drink only bottled or purified water. Your safari operator will provide safe drinking water.</li>
  <li><strong>Food:</strong> Eat at your lodge or camp where food preparation standards are maintained. Avoid street food unless you have a strong stomach.</li>
  <li><strong>Sun protection:</strong> The equatorial sun is intense, especially at altitude. SPF 50+ sunscreen, hat, and sunglasses are essential.</li>
  <li><strong>Altitude:</strong> The Ngorongoro Crater rim sits at 2,235m and Kilimanjaro reaches 5,895m. Allow time to acclimatize if coming from sea level.</li>
</ul>

<h2>Pre-Trip Health Checklist</h2>
<ol>
  <li>Visit a travel health clinic 4-6 weeks before departure</li>
  <li>Get recommended vaccinations and obtain your yellow card</li>
  <li>Start antimalarial medication as prescribed</li>
  <li>Purchase comprehensive travel insurance with evacuation coverage</li>
  <li>Pack a personal first aid kit (see our <a href="blog-post.php?slug=tanzania-safari-packing-list">safari packing list</a>)</li>
  <li>Register with your embassy or consulate (optional but recommended)</li>
  <li>Share your itinerary with family/friends at home</li>
</ol>

