<h2>Mid-Range Safari: $400-800 Per Person Per Day</h2>
<p>Mid-range safaris hit the sweet spot for most travelers. Accommodation upgrades to well-appointed lodges with swimming pools, en-suite bathrooms, and often stunning locations overlooking the bush or crater rim.</p>
<p><strong>Key upgrades from budget:</strong></p>
<ul>
  <li>Private safari vehicle for your group</li>
  <li>Higher-quality lodges with en-suite facilities</li>
  <li>Better locations closer to prime wildlife areas</li>
  <li>More flexible game drive schedules</li>
  <li>Welcome drinks, afternoon tea, and higher meal quality</li>
</ul>
<p>A typical 6-day mid-range safari covering the Northern Circuit (Tarangire, Serengeti, Ngorongoro) costs $2,300-2,940 per person depending on group size and season.</p>

<h2>Luxury Safari: $600-1,500+ Per Person Per Day</h2>
<p>Luxury safaris represent the pinnacle of the Tanzania safari experience. Stay in award-winning lodges and intimate tented camps with gourmet dining, private guides, and exclusive wilderness locations far from the crowds.</p>
<p><strong>Luxury inclusions:</strong></p>
<ul>
  <li>Private 4x4 vehicle with dedicated expert guide</li>
  <li>Premium lodges or luxury mobile/tented camps</li>
  <li>Gourmet meals with premium wines and spirits</li>
  <li>Bush breakfasts, sundowner cocktails, and surprise bush dining</li>
  <li>Walking safaris, night game drives (where permitted)</li>
  <li>Domestic flights between parks (saving travel time)</li>
  <li>Laundry service and all gratuities</li>
</ul>
<p>Premium 6-day itineraries start from $4,045 per person and can exceed $8,000+ for ultra-luxury fly-in safaris with exclusive-use camps.</p>

