<h2>Money-Saving Tips for Your Tanzania Safari</h2>
<ol>
  <li><strong>Travel in the green season</strong> (April-May, November) for 30-40% lower rates with surprisingly good wildlife viewing</li>
  <li><strong>Increase your group size</strong> — safari costs per person drop significantly when sharing a vehicle with 4-6 people</li>
  <li><strong>Book early</strong> — the best lodges and camps offer early-bird discounts 6-12 months in advance</li>
  <li><strong>Mix accommodation levels</strong> — stay at a luxury lodge for your Ngorongoro night and budget camps for the Serengeti</li>
