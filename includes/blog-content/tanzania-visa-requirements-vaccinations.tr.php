<h2>Sıtma Önleme</h2>
<p>Sıtma, safari destinasyonları ve Zanzibar dahil Tanzanya genelinde görülmektedir. Önleme son derece önemlidir:</p>
<p><strong>Sıtma önleyici ilaç seçenekleri:</strong></p>
<ul>
  <li><strong>Malarone (Atovaquone/Proguanil):</strong> En yaygın tercih. 1-2 gün önce başlanır, ayrılıştan 7 gün sonra kesilir. Yan etkiler minimal.</li>
  <li><strong>Doksisiklin:</strong> Uygun fiyatlı alternatif. 1-2 gün önce başlanır, ayrılıştan 28 gün sonra kesilir. Güneş hassasiyetine yol açabilir.</li>
  <li><strong>Mefloquine (Lariam):</strong> Haftalık dozaj; ancak nöropsikiyatrik yan etki potansiyeli mevcuttur. Doktorunuzla görüşün.</li>
</ul>
<p><strong>Ek sıtma önleme tedbirleri:</strong></p>
<ul>
  <li>Açık tenli alanlara, özellikle alacakaranlık ve şafak vakitlerinde, DEET bazlı böcek kovucu (%30 ve üzeri) uygulayın</li>
  <li>Akşamları uzun kollu gömlek ve pantolon giyin</li>
  <li>Cibinlik altında uyuyun (safari kamplarında ve lojlarda neredeyse her yerde mevcuttur)</li>
  <li>Odanızda sivrisinek kıvrımı veya prize takılan kovucu kullanın</li>
</ul>

<h2>Seyahat Sigortası</h2>
<p>Kapsamlı seyahat sigortası Tanzanya için yasal açıdan zorunlu değildir; ancak kesinlikle tavsiye edilir. Kaliteli bir poliçe aşağıdakileri kapsamalıdır:</p>
<ul>
  <li><strong>Tıbbi tahliye kapsamı</strong> — Hastanelere uzak safari bölgeleri için kritiktir. Serengeti'den Nairobi'ye helikopter tahliyesi 10.000 Doların üzerine çıkabilir.</li>
  <li><strong>Acil tıbbi tedavi</strong> — Hastaneye yatış için en az 100.000 Dolar kapsam</li>
  <li><strong>Seyahat iptali/kesintisi</strong> — Planlar değişirse yatırımınızı korur</li>
  <li><strong>Bagaj kaybı/gecikmesi</strong> — Küçük uçaklardaki iç hat uçuşlarında zaman zaman bagaj kayması yaşanabilir</li>
  <li><strong>Yüksek irtifa kapsamı</strong> — <a href="blog-post.php?slug=mount-kilimanjaro-climbing-guide">Kilimanjaro tırmanışı</a> planlıyorsanız poliçenizin 6.000 m'ye kadar irtifayı kapsadığından emin olun</li>
</ul>

<h2>Tanzanya İçin Diğer Sağlık İpuçları</h2>
<ul>
  <li><strong>Su:</strong> Yalnızca şişelenmiş veya arıtılmış su için. Safari operatörünüz güvenli içme suyu sağlayacaktır.</li>
  <li><strong>Yiyecek:</strong> Gıda hazırlama standartlarının korunduğu loj veya kampınızda yiyin. Güçlü bir mideye sahip değilseniz sokak yemeklerinden kaçının.</li>
  <li><strong>Güneşten korunma:</strong> Özellikle yüksek irtifada ekvator güneşi son derece yoğundur. SPF 50+ güneş kremi, şapka ve güneş gözlüğü zorunludur.</li>
  <li><strong>İrtifa:</strong> Ngorongoro Krateri ağzı 2.235 m'de, Kilimanjaro ise 5.895 m'de bulunur. Deniz seviyesinden geliyorsanız aklimasyon için zaman tanıyın.</li>
</ul>

