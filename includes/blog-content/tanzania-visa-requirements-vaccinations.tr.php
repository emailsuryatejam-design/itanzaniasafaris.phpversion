<p>Bir Tanzanya safarisi planlamak, park ve konaklama seçiminin çok ötesine geçer. Giriş gereksinimleri, vize prosedürleri ve sağlık hazırlıkları, sorunsuz bir seyahat için vazgeçilmezdir. Bu rehber, vize başvurularından aşılamalara, sıtma önlemeye ve seyahat sigortasına kadar her konuyu ele alarak Tanzanya'ya tamamen hazırlıklı varmanızı sağlar.</p>

<h2>Tanzanya Vize Gereksinimleri</h2>
<p>Uluslararası ziyaretçilerin büyük çoğunluğunun Tanzanya'ya giriş için vize alması gerekmektedir. İyi haber şu ki süreç oldukça basittir; seyahat öncesi online veya varışta tamamlanabilir.</p>

<h3>E-Vize (Önerilir)</h3>
<p>Tanzanya'nın elektronik vize sistemi, seyahatten önce online başvuru yapmanıza imkan tanır. Bu en verimli seçenektir:</p>
<ul>
  <li><strong>Başvuru sitesi:</strong> visa.immigration.go.tz</li>
  <li><strong>İşlem süresi:</strong> 3-10 iş günü (seyahatten en az 2 hafta önce başvurun)</li>
  <li><strong>Ücret:</strong> 50 ABD Doları (tek giriş, 90 gün) veya 100 ABD Doları (çok girişli)</li>
  <li><strong>Gereksinimler:</strong> Pasaport taraması, vesikalık fotoğraf, dönüş uçuşu bilgileri, konaklama kanıtı</li>
</ul>

<h3>Varışta Vize</h3>
<p>Vizeler Kilimanjaro Uluslararası Havalimanı (JRO), Julius Nyerere Havalimanı (DAR) ve büyük kara sınır kapılarında alınabilir:</p>
<ul>
  <li><strong>Ücret:</strong> 50 ABD Doları (tek giriş) — ABD vatandaşları 100 ABD Doları öder</li>
  <li><strong>Ödeme:</strong> Yalnızca nakit ABD Doları (2006 veya sonrası basılmış, temiz ve hasarsız banknotlar)</li>
  <li><strong>Bekleme süresi:</strong> Kuyruğa bağlı olarak 30-60 dakika</li>
</ul>
<p>Olası kuyruklardan kaçınmak ve uçağa binmeden önce vizenizin onaylandığından emin olmak için e-vize sistemini öneriyoruz.</p>

<h3>Pasaport Gereksinimleri</h3>
<ul>
  <li>Giriş tarihinizden itibaren en az 6 ay geçerli olmalı</li>
  <li>Damgalar için en az 2 boş sayfa bulunmalı</li>
  <li>İyi durumda olmalı (önemli hasar veya ıslaklık izi bulunmamalı)</li>
</ul>

<h2>Sarı Humma Aşısı</h2>
<p>Tanzanya, sarı hummanın endemik olduğu bir ülkeden geliyorsanız veya bu tür bir ülkeden transit geçiş yaptıysanız sarı humma aşısı belgesini şart koşmaktadır. Bu; Sahra altı Afrika ve Güney Amerika'daki pek çok ülkeyi kapsar.</p>
<p><strong>Temel bilgiler:</strong></p>
<ul>
  <li>Avrupa, Kuzey Amerika, Asya veya Orta Doğu'dan direkt uçuş yapıyorsanız sarı humma aşısı <strong>zorunlu değil</strong> ancak tavsiye edilir</li>
  <li>Etiyopya, Kenya veya başka bir endemik ülkeden transit geçiş yapıyorsanız geçerli sarı humma sertifikası <strong>zorunludur</strong></li>
  <li>Aşının varıştan en az 10 gün önce yapılmış olması gerekir</li>
  <li>Uluslararası Aşılanma Sertifikası (sarı kart) ömür boyu geçerlidir</li>
  <li>Zanzibar'ın kendine özgü giriş gereksinimleri vardır; anakaradan gelenler dahil ziyaretçilerin sarı humma sertifikaları genellikle kontrol edilir</li>
</ul>

