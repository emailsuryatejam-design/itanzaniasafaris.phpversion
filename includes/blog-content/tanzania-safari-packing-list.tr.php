<p>Bir Tanzanya safarisi için bavul hazırlamak, sıradan bir tatil hazırlığından çok daha fazla düşünce gerektirir. Tozlu çalılık yolları, yoğun ekvator güneşi, serin yayla sabahları ve tropikal yağmurla karşılaşabilirsiniz — hepsini bazen aynı gün içinde. Bu uzman el kitabı, Doğu Afrika maceranızda aşırıya kaçmadan ihtiyaç duyduğunuz her şeye sahip olmanızı sağlar.</p>

<h2>Giysi Temelleri: Safaride Ne Giymeli?</h2>
<p>Safari kıyafetleri nötr renkli, hafif, nefes alabilir ve hızlı kuruyan özellikte olmalıdır. Parlak beyazdan kaçının (çe sineğini çeker ve hızla kirlenir) ve koyu lacivert ya da siyahtan uzak durun (hem ısıyı emer hem de çe sineğini çeker).</p>
<p><strong>Önerilen kıyafetler:</strong></p>
<ul>
  <li><strong>3-4 hafif gömlek/bluz</strong>, haki, zeytin yeşili, ten rengi veya ada yeşilinde. Uzun kollar güneş ve böcek koruması sağlar.</li>
  <li><strong>2-3 safari pantolonu</strong> — fermuarlı ve çıkarılabilir paçalı dönüştürülebilir modeller çok yönlülük açısından idealdir</li>
  <li><strong>1 sıcak polar ceket</strong> — Ngorongoro Krateri ağzında (2.235 m irtifa) sabahlar gerçekten soğuk olabilir, 5°C'ye kadar düşer</li>
  <li><strong>1 yağmurluk</strong> — hafif ve katlanabilir; yeşil sezonda vazgeçilmez</li>
  <li><strong>1 geniş kenarlı şapka</strong> — açık çatılı game drive'larda güneşten korunmak için kritik öneme sahiptir</li>
  <li><strong>Rahat yürüyüş ayakkabıları</strong> — kapalı burunlu, alıştırılmış; doğa yürüyüşleri ve kamp ortamı için uygun</li>
  <li><strong>Sandalet veya terlik</strong> — lojunuzda ya da kamp alanında dinlenmek için</li>
  <li><strong>Sıcak çorap ve pijama</strong> — yayla kampları gece soğuyabilir</li>
  <li><strong>Mayo</strong> — birçok lojda yüzme havuzu bulunur</li>
</ul>
<p><strong>Önemli:</strong> Tanzanya'da iç hat uçuşlarında bagaj sınırı genellikle 15-20 kg olup küçük uçaklarda sert bavul kabul edilmemektedir. Hafif ve pratik paket yapın.</p>

<h2>Kamera ve Fotoğraf Ekipmanı</h2>
<p>Tanzanya, dünyanın en iyi yaban hayatı fotoğrafçılığı fırsatlarından bazılarını sunar. Önerimiz şu şekildedir:</p>
<ul>
  <li><strong>Kamera gövdesi:</strong> İyi otomatik odak performansına sahip DSLR veya aynasız</li>
  <li><strong>Telefoto zoom lens:</strong> Yaban hayatı için 100-400 mm veya 200-600 mm idealdır</li>
  <li><strong>Geniş açı lens:</strong> Manzaralar, yıldız fotoğrafçılığı ve kamp/loj çekimleri için</li>
  <li><strong>Ekstra piller:</strong> En az 2-3 yedek getirin. Çalılık kamplarda şarj imkanı sınırlı olabilir.</li>
  <li><strong>Hafıza kartları:</strong> 128 GB ve üzeri önerilir. Binlerce fotoğraf çekeceksiniz.</li>
  <li><strong>Fasulye torbası veya pencere montajı:</strong> Aracın çatısında ya da pencere çerçevesinde lensinizi dengelemek için</li>
  <li><strong>Toz koruması:</strong> Plastik torbalar, lens bezleri ve sensör temizleme körüğü</li>
  <li><strong>Dürbün:</strong> Uzak yaban hayatı ve kuş gözlemi için 8x42 veya 10x42 idealdir</li>
</ul>
<p>Akıllı telefon kullananlar için modern zoom lensli telefonlar mükemmel safari görüntüleri üretebilir. Küçük bir telefon tripod adaptörü ve cep dürbünü sonuçlarınızı önemli ölçüde iyileştirir.</p>

<h2>Sağlık ve Güvenlik Malzemeleri</h2>
<ul>
  <li><strong>Reçeteli ilaçlar</strong> — gecikmeler ihtimaline karşı fazladan getirin</li>
  <li><strong>Sıtma profilaksisi</strong> — seyahatten en az 4-6 hafta önce doktorunuza danışın. <a href="blog-post.php?slug=tanzania-visa-requirements-vaccinations">Tanzanya sağlık gereksinimleri rehberimize</a> bakın.</li>
  <li><strong>Böcek kovucu</strong> — %30 veya daha yüksek oranda DEET ya da piretirine bazlı. Akşam saatlerinde zorunludur.</li>
  <li><strong>Güneş kremi</strong> — SPF 50+. Ekvator güneşi özellikle yüksek irtifada son derece yoğundur.</li>
  <li><strong>Temel ilk yardım kiti</strong> — yara bantları, antiseptik, ağrı kesici, ishal önleyici ilaç</li>
  <li><strong>El dezenfektanı</strong> — el yıkamanın mümkün olmadığı game drive'larda faydalıdır</li>
  <li><strong>Rehidrasyon tuzları</strong> — yoğun sıcak ve uzun game drive'lar için</li>
  <li><strong>SPF'li dudak balsamı</strong> — sıklıkla göz ardı edilse de son derece önemlidir</li>
</ul>

<h2>Belgeler ve Para</h2>
<ul>
