  <li><strong>Sarı humma sertifikası</strong> — endemik bir ülkeden geliyorsanız zorunlu</li>
  <li><strong>Seyahat sigortası belgeleri</strong> — tıbbi tahliye kapsamı dahil (kesinlikle önerilir)</li>
  <li><strong>Rezervasyonların çıktıları</strong> — otel onayları, safari güzergahı, uçuş biletleri</li>
  <li><strong>ABD Doları</strong> — bahşişler, varışta vize ve küçük alışverişler için. 2006 sonrası basılmış, temiz banknotlar getirin.</li>
  <li><strong>Kredi/banka kartı</strong> — Tanzanya'da Visa en yaygın kabul gören karttır. Arusha'da ATM mevcuttur.</li>
</ul>

<h2>Diğer Safari Temel Malzemeleri</h2>
<ul>
  <li><strong>Kafa feneri veya küçük el feneri</strong> — kamplar ve lojlar gece sınırlı aydınlatmaya sahip olabilir</li>
  <li><strong>Powerbank</strong> — uzun game drive'larda cihazları şarj etmek için</li>
  <li><strong>Evrensel adaptör</strong> — Tanzanya'da İngiltere tipi G fişler (3 pinli) kullanılır</li>
  <li><strong>Su geçirmez torba veya fermuarlı poşetler</strong> — elektronik cihazları toz ve yağmurdan korumak için</li>
  <li><strong>Yeniden doldurulabilir su şişesi</strong> — safari araçlarının büyük çoğunluğu soğutucuda su taşır; kendi şişeniz konforu artırır</li>
  <li><strong>Küçük günlük sırt çantası</strong> — game drive'larda ve doğa yürüyüşlerinde temel malzemeleri taşımak için</li>
</ul>

<h2>Sezona Özel Paket İpuçları</h2>
<p><strong>Kurak mevsim (Haziran-Ekim):</strong> Tozlu koşullar — ekstra lens bezi, yüz için boyunluk/bandana ve tozu daha az gösteren koyu nötr tonlu kıyafetler getirin. Özellikle yüksek irtifada sabahlar serin olabilir (10-15°C).</p>
<p><strong>Yeşil mevsim (Kasım-Mayıs):</strong> Kaliteli bir yağmurluk, su geçirmez kamera çantası koruması ve böcek kovucu unutmayın. Yollar çamurlu olabilir. Ödülü: bereketli, fotoğraflık manzaralar ve dramatik fırtına gökyüzleri.</p>

