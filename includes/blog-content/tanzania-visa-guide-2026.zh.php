<h2>落地签证：仍可选择，但注意事项较多</h2>
<p>落地签证在以下口岸提供：</p>
<ul>
  <li>乞力马扎罗国际机场（JRO）——阿鲁沙和北部线路游猎的主要入境口岸</li>
  <li>朱利叶斯·尼雷尔国际机场（DAR）——达累斯萨拉姆，南部线路和桑给巴尔的入境口岸</li>
  <li>桑给巴尔国际机场（ZNZ）</li>
  <li>主要陆路边境口岸</li>
</ul>
<p><strong>落地签注意事项：</strong></p>
<ul>
  <li>仅接受2006年以后版本的完好美元现金（破损、折叠痕迹过多的纸币可能被拒收）</li>
  <li>旺季（7至9月）排队时间可能长达1至2小时</li>
  <li>建议准备好所有材料（护照、酒店确认件、回程机票）以加快办理速度</li>
</ul>

<h2>护照要求</h2>
<ul>
  <li><strong>有效期：</strong>护照有效期必须超过预定离境日期后至少6个月。若护照有效期不足，即使签证申请获批，入境时也可能被拒绝，请务必提前更换护照。</li>
  <li><strong>空白页：</strong>至少须有2页（最好4页）完整空白页用于盖章</li>
  <li><strong>护照状态：</strong>护照须完好无损，不可有严重破损、缺页或大面积水渍</li>
  <li><strong>儿童护照：</strong>每个儿童均须持有独立护照（不可添附于父母护照）</li>
</ul>

<h2>黄热病疫苗：重要的强制要求</h2>
<p>黄热病疫苗要求是中国旅行者最常忽略的细节之一，处理不当可能直接导致入境被拒。</p>
<p><strong>必须持有黄热病疫苗接种证书（黄卡）的情况：</strong></p>
<ul>
  <li>从黄热病疫区国家（包括部分非洲和南美洲国家）直飞或经停坦桑尼亚</li>
  <li>在抵达坦桑尼亚前曾经停上述疫区国家，即使只是短暂过境</li>
</ul>
<p><strong>从中国直飞坦桑尼亚（不经停疫区）：</strong>通常不要求黄热病证书，但以下情况除外：</p>
<ul>
  <li>若经停肯尼亚内罗毕（虽有争议，但建议携带黄卡以防万一）</li>
  <li>前往桑给巴尔——桑给巴尔当局有时独立检查黄热病证书</li>
</ul>
<p><strong>我们的强烈建议：</strong>无论是否严格要求，所有前往坦桑尼亚的旅行者均应在出发前至少10天接种黄热病疫苗，并随身携带黄卡原件。黄热病疫苗可在国内各地区设有国际旅行健康证明资质的医院和卫生机构接种，终身有效，一次接种永久保护。</p>

<h2>桑给巴尔特殊入境规则</h2>
<p>桑给巴尔是坦桑尼亚的自治区，有其独立的入境管理机制：</p>
<ul>
  <li><strong>签证：</strong>持坦桑尼亚有效旅游签证可直接入境桑给巴尔，无需额外签证</li>
  <li><strong>黄热病证书：</strong>桑给巴尔海关可能独立检查，即使您已从坦桑尼亚大陆抵达</li>
  <li><strong>入境卡：</strong>抵达桑给巴尔时需填写独立的入境卡（区别于坦桑尼亚大陆入境卡）</li>
  <li><strong>护照盖章：</strong>桑给巴尔入境会额外加盖印章</li>
</ul>

