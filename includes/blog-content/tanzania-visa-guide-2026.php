<h2>Visa Extension</h2>

<p>If you wish to extend your stay in Tanzania beyond the initial visa period, extensions are processed by the Tanzania Immigration Department. The main immigration office is in Dar es Salaam; regional offices are in Arusha, Moshi, Zanzibar, and other cities.</p>
<ul>
  <li>Extensions are typically granted for up to 90 additional days</li>
  <li>Apply in person at an immigration office before your current visa expires</li>
  <li>Bring your passport, current visa, evidence of funds, and a reason for extension</li>
  <li>Extension fee: typically $50 for a 90-day extension (confirm current fee at time of application)</li>
</ul>

<h2>KAZA Uni-Visa: Combining Tanzania with Zambia &amp; Zimbabwe</h2>

<p>If your itinerary includes Tanzania combined with Victoria Falls (either the Zambia or Zimbabwe side), the <strong>KAZA Univisa</strong> may be relevant — however, it is important to note that the KAZA Univisa covers Zambia and Zimbabwe only; Tanzania is not a KAZA member. Tanzania requires its own separate visa. Travelers doing a combined Tanzania + Zambia/Zimbabwe itinerary need both a Tanzania visa and a KAZA Univisa (or individual Zambia/Zimbabwe visas).</p>

<h2>East African Tourist Visa</h2>

<p>Tanzania, Uganda, and Rwanda offer a shared <strong>East African Tourist Visa</strong> that allows entry to all three countries for $100. This is excellent value for travelers planning to combine Tanzania with gorilla trekking in Uganda or Rwanda. Note that Kenya is not included in this visa arrangement. The East African Tourist Visa can be applied for at the first port of entry among the three countries, or in some cases online.</p>

<h2>Common Visa Mistakes to Avoid</h2>

<ul>
  <li><strong>Applying too late:</strong> Apply at minimum 2–3 weeks before departure. Processing delays do occur, and resolving issues is far easier before you're at the airport.</li>
  <li><strong>Incorrect name spelling:</strong> The name on your visa application must exactly match your passport. Even minor differences (middle name included/excluded, hyphenated names) can cause problems. Check carefully before submitting.</li>
  <li><strong>Passport photos not meeting specification:</strong> Blurry, poorly lit, or incorrectly formatted photos are the most common cause of application delays. Use a professional or a reliable digital passport photo tool.</li>
  <li><strong>Booking visa fees through unofficial third-party sites:</strong> Several websites charge $80–$150+ to "process" Tanzania visas. The official government fee is $50. Use only the official portal.</li>
  <li><strong>Not printing the e-Visa:</strong> Tanzania immigration requires a physical printout. Do not assume showing it on your phone is acceptable — print it.</li>
  <li><strong>Arriving without sufficient USD cash:</strong> If opting for visa on arrival, bring exact change or close to it in USD. Large denomination bills may not be changed at the immigration counter.</li>
  <li><strong>Forgetting the yellow fever certificate:</strong> Even if you believe it's not required for your nationality/origin, carry it. The inconvenience of being asked for it without it available is real.</li>
  <li><strong>Passport not valid for 6 months:</strong> Check your passport expiry date before booking any travel to Tanzania.</li>
</ul>

<h2>Entry at Kilimanjaro Airport vs Dar es Salaam</h2>

<p>For northern circuit safaris (Serengeti, Ngorongoro, Tarangire, Kilimanjaro), fly into <strong>Kilimanjaro International Airport (JRO)</strong>, which serves Arusha directly. For southern circuit safaris (Nyerere, Ruaha) or if flying to Zanzibar as a starting point, use <strong>Julius Nyerere International Airport in Dar es Salaam (DAR)</strong>. Zanzibar has its own international airport (ZNZ) with connections to Nairobi, Addis Ababa, Doha, and Dubai, which is a convenient starting point for beach-first itineraries.</p>

