<h2>How to Apply for a Tanzania e-Visa: Step by Step</h2>

<h3>Step 1: Create an Account</h3>
<p>Visit evisa.immigration.go.tz and create an account using your email address. You will receive a verification email — click the link to activate your account before proceeding.</p>

<h3>Step 2: Complete the Application Form</h3>
<p>Fill in the online application form with:</p>
<ul>
  <li>Full name (exactly as it appears in your passport)</li>
  <li>Date of birth, nationality, and passport details</li>
  <li>Purpose of visit (select "Tourism")</li>
  <li>Intended entry date and departure date</li>
  <li>Accommodation details (your first Tanzania hotel or camp)</li>
  <li>Previous visits to Tanzania (if any)</li>
</ul>

<h3>Step 3: Upload Required Documents</h3>
<p>The following documents must be uploaded as clear, legible scans or photos:</p>
<ul>
  <li><strong>Passport bio page:</strong> Must show your full name, photo, passport number, nationality, and expiry date clearly. Passport must be valid for at least 6 months beyond your intended departure date from Tanzania.</li>
  <li><strong>Passport-style photo:</strong> Recent, clear, against a white or light background. Face must be fully visible, no glasses, no hats. Digital passport photos from photo apps are generally acceptable if they meet the specification.</li>
  <li><strong>Return/onward flight ticket:</strong> A confirmed booking showing your arrival into Tanzania and departure out of Tanzania (or onward to another country). If your exact return ticket is not yet booked, a tentative booking confirmation or itinerary from your travel agent may be accepted, though confirmed bookings are preferred.</li>
  <li><strong>Proof of accommodation:</strong> Hotel booking confirmation or a letter from your safari operator confirming your itinerary. iTanzania Safaris provides this documentation as standard for all clients.</li>
  <li><strong>Bank statement or proof of sufficient funds:</strong> A recent bank statement (within 3 months) showing sufficient funds to support your stay. Tanzania immigration guidelines suggest approximately $50 per day as a general benchmark.</li>
  <li><strong>Travel itinerary:</strong> A brief outline of your planned activities in Tanzania</li>
</ul>

<h3>Step 4: Pay the Visa Fee</h3>
<p>Payment is made online by debit or credit card (Visa, Mastercard, and American Express accepted). The payment system uses a secure gateway. Keep your payment receipt/confirmation.</p>

<h3>Step 5: Wait for Processing</h3>
<p>Standard processing time is <strong>3–5 business days</strong>. Many applications are processed within 2 business days. During peak periods (July–August, December–January) processing may take longer. Apply at least 2–3 weeks before your travel date to allow comfortable buffer time.</p>

<h3>Step 6: Download &amp; Print Your e-Visa</h3>
<p>Once approved, you will receive an email notification. Log back into the portal and download your e-Visa as a PDF. <strong>Print at least two copies</strong> — one to present at immigration and one as backup. Also save a digital copy on your phone. Tanzania immigration officials will want to see the physical printout at the port of entry.</p>

<h2>Visa on Arrival</h2>

<p>Visa on arrival is still available at Tanzania's major international entry points:</p>
<ul>
  <li>Julius Nyerere International Airport, Dar es Salaam (DAR)</li>
  <li>Kilimanjaro International Airport (JRO)</li>
  <li>Zanzibar Karume International Airport (ZNZ)</li>
  <li>Major land border crossings including Namanga (Kenya border)</li>
</ul>

<p>Payment for visa on arrival is in <strong>USD cash only</strong> at most entry points (some now accept cards, but cash is safer). Visa on arrival costs the same as the e-Visa ($50/$100). Queues at major airports during busy periods can be long — sometimes 1–2 hours. The e-Visa is strongly recommended to avoid this and to resolve any documentation queries before you depart your home country.</p>

<h2>Tanzania Visa Validity &amp; Entry Rules</h2>

<h3>90-Day Stay</h3>
<p>The standard tourist visa allows a stay of up to 90 days from the date of entry. You do not need to stay 90 days — the visa simply permits you to remain for up to that duration. Most safari visitors stay 7–21 days, well within this limit.</p>

<h3>Zanzibar: Same Visa, Same Immigration Area</h3>
<p>A common source of confusion: Zanzibar (the Zanzibar Archipelago, comprising Unguja and Pemba islands) is a semi-autonomous region of Tanzania but shares the same immigration system as the mainland. <strong>Your Tanzania visa covers both the mainland and Zanzibar.</strong> You do not need a separate visa for Zanzibar. However, when you fly from the mainland to Zanzibar, you pass through a Zanzibar immigration desk where your documents are checked — have your passport and visa ready. This is not a separate visa issuance, simply a documentation check.</p>

<h3>Passport Validity Requirement</h3>
<p>Your passport must be valid for at least <strong>6 months beyond your intended departure date from Tanzania</strong>. This is strictly enforced. A passport expiring within 6 months of your departure will result in refusal of entry. Check and renew your passport well before booking international travel.</p>

<h2>Yellow Fever Vaccination Certificate</h2>

<p>Tanzania requires proof of yellow fever vaccination for travelers arriving from (or having transited through) yellow fever-endemic countries. The list of endemic countries includes most of Sub-Saharan Africa and parts of South America.</p>

<p>The yellow fever vaccination certificate (Carte Jaune / International Certificate of Vaccination or Prophylaxis — ICVP) is issued when you receive the yellow fever vaccine. The vaccine provides lifelong protection after a single dose (WHO updated guidance, 2016). Keep your original yellow fever certificate with your travel documents.</p>

<p><strong>Practical advice:</strong> Even if you are not arriving from an endemic country, carry your yellow fever certificate. Immigration officers in Tanzania (particularly in Zanzibar) occasionally request it regardless of origin, and being unable to produce it can cause delays or refusal of entry.</p>

