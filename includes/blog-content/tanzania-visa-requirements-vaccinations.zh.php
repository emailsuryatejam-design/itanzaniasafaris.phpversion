<h2>疟疾预防</h2>
<p>疟疾广泛存在于坦桑尼亚全境，包括游猎目的地和桑给巴尔。预防措施至关重要：</p>
<p><strong>抗疟疾药物选择：</strong></p>
<ul>
  <li><strong>马拉龙（阿托伐醌/氯胍）：</strong>最受欢迎的选择。出发前1至2天开始服用，离境后继续服用7天，副作用较少。</li>
  <li><strong>多西环素：</strong>经济实惠的替代品。出发前1至2天开始，离境后继续服用28天。可能导致对阳光敏感。</li>
  <li><strong>甲氟喹（Lariam）：</strong>每周服用一次，但可能有神经精神方面的副作用，请与医生充分讨论。</li>
</ul>
<p><strong>其他疟疾预防措施：</strong></p>
<ul>
  <li>在裸露皮肤上喷涂含30%以上DEET的驱虫剂，尤其在黄昏和黎明时分</li>
  <li>傍晚时分穿着长袖上衣和长裤</li>
  <li>在蚊帐下睡觉（几乎所有游猎营地和旅馆均提供）</li>
  <li>在房间内使用蚊香或电子驱蚊器</li>
</ul>

<h2>旅行保险</h2>
<p>坦桑尼亚法律上并不强制要求旅行保险，但强烈建议购买。优质保险方案应包含：</p>
<ul>
  <li><strong>医疗紧急撤离保障</strong>——对于远离医院的游猎区域至关重要。从塞伦盖蒂直升机撤离至内罗毕的费用可高达10,000美元以上。</li>
  <li><strong>紧急医疗救治</strong>——住院保障至少10万美元</li>
  <li><strong>行程取消/中断保障</strong>——保护您的行程投资免遭计划变更损失</li>
  <li><strong>行李丢失/延误保障</strong>——小型飞机的国内航班偶尔会出现行李中转问题</li>
  <li><strong>高海拔保障</strong>——若<a href="blog-post.php?slug=mount-kilimanjaro-climbing-guide">攀登乞力马扎罗</a>，请确认保险覆盖6,000米以上海拔</li>
</ul>

<h2>其他坦桑尼亚健康建议</h2>
<ul>
  <li><strong>饮水：</strong>只饮用瓶装或净化水，您的游猎运营商将提供安全饮用水。</li>
  <li><strong>饮食：</strong>在旅馆或营地就餐，食品卫生标准有保障。非特殊情况建议避免路边摊食物。</li>
  <li><strong>防晒：</strong>赤道阳光强烈，高海拔地区尤甚。SPF 50以上防晒霜、帽子和太阳镜缺一不可。</li>
  <li><strong>高海拔：</strong>恩戈罗恩戈罗火山口边缘海拔2,235米，乞力马扎罗山顶高达5,895米。从海平面出发的旅行者需留出适应时间。</li>
</ul>

