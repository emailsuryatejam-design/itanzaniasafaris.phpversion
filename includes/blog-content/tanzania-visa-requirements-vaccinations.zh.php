<h2>坦桑尼亚签证要求</h2>
<p>大多数国际游客需要持签证入境坦桑尼亚。好消息是，手续简便，可在网上提前办理或抵达时落地办理。</p>

<h3>电子签证（推荐方式）</h3>
<p>坦桑尼亚电子签证系统允许您在出发前在线申请，是最高效的方式：</p>
<ul>
  <li><strong>申请网址：</strong>visa.immigration.go.tz</li>
  <li><strong>处理时间：</strong>3至10个工作日（建议至少提前2周申请）</li>
  <li><strong>费用：</strong>50美元（单次入境，有效期90天）或100美元（多次入境）</li>
  <li><strong>所需材料：</strong>护照扫描件、证件照、回程机票信息、住宿证明</li>
</ul>

<h3>落地签证</h3>
<p>可在乞力马扎罗国际机场（JRO）、朱利叶斯·尼雷尔机场（DAR）及主要陆路口岸办理：</p>
<ul>
  <li><strong>费用：</strong>50美元（单次入境）——美国公民需缴纳100美元</li>
  <li><strong>支付方式：</strong>仅接受美元现金（需为2006年以后版本的完好纸币）</li>
  <li><strong>等待时间：</strong>视队伍长短，约30至60分钟</li>
</ul>
<p>我们建议提前申请电子签证，以避免排队等候，并确保签证在登机前已获批准。</p>

<h3>护照要求</h3>
<ul>
  <li>有效期须超过入境日期后至少6个月</li>
  <li>至少留有2页空白页供盖章</li>
  <li>护照须完好无损（不可有明显破损或水渍）</li>
</ul>

<h2>黄热病疫苗接种</h2>
<p>如果您是从黄热病疫区国家入境或曾在途中经停，坦桑尼亚要求提供黄热病疫苗接种证明。这包括撒哈拉以南非洲和南美洲的许多国家。</p>
<p><strong>关键要点：</strong></p>
<ul>
  <li>若直接从欧洲、北美、亚洲或中东出发，黄热病疫苗<strong>非强制要求</strong>，但建议接种</li>
  <li>若途经埃塞俄比亚、肯尼亚或其他疫区国家，<strong>必须</strong>持有效黄热病证书</li>
  <li>疫苗必须在抵达前至少10天接种</li>
  <li>国际疫苗接种证书（黄卡）终身有效</li>
  <li>桑给巴尔有独立的入境规定，通常对从坦桑尼亚大陆抵达的旅客也会查验黄热病证书</li>
</ul>

