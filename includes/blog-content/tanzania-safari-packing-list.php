<p>Packing for a Tanzania safari requires more thought than a typical vacation. You will encounter dusty bush roads, intense equatorial sun, cool highland mornings, and possibly tropical rain — sometimes all in the same day. This expert-curated packing list ensures you have everything you need without overpacking for your East African adventure.</p>

<h2>Clothing Essentials: What to Wear on Safari</h2>
<p>Safari clothing should be neutral-colored, lightweight, breathable, and quick-drying. Avoid bright whites (they attract tsetse flies and get dirty quickly) and dark navy or black (they absorb heat and also attract tsetse flies).</p>
<p><strong>Recommended clothing:</strong></p>
<ul>
  <li><strong>3-4 lightweight shirts/tops</strong> in khaki, olive, tan, or sage green. Long sleeves offer sun and insect protection.</li>
  <li><strong>2-3 pairs of safari trousers</strong> — zip-off convertible pants are ideal for versatility</li>
  <li><strong>1 warm fleece or jacket</strong> — mornings on the Ngorongoro Crater rim (2,235m altitude) can be genuinely cold, as low as 5°C</li>
  <li><strong>1 rain jacket</strong> — lightweight and packable, essential during green season</li>
  <li><strong>1 wide-brimmed hat</strong> — critical for sun protection during open-roof game drives</li>
  <li><strong>Comfortable walking shoes</strong> — closed-toe, broken-in, for nature walks and camp environments</li>
  <li><strong>Sandals or flip-flops</strong> — for relaxing at your lodge or camp</li>
  <li><strong>Warm socks and sleepwear</strong> — highland camps can be cool at night</li>
  <li><strong>Swimwear</strong> — many lodges have swimming pools</li>
</ul>
<p><strong>Important:</strong> Domestic flight luggage allowances in Tanzania are typically 15-20kg in soft-sided bags (no hard suitcases on small planes). Pack light and practical.</p>

<h2>Camera and Photography Gear</h2>
<p>Tanzania offers some of the best wildlife photography opportunities in the world. Here is what we recommend:</p>
<ul>
  <li><strong>Camera body:</strong> DSLR or mirrorless with good autofocus performance</li>
  <li><strong>Telephoto zoom lens:</strong> 100-400mm or 200-600mm is the sweet spot for wildlife</li>
  <li><strong>Wide-angle lens:</strong> For landscapes, starscapes, and camp/lodge shots</li>
  <li><strong>Extra batteries:</strong> Bring at least 2-3 spares. Charging may be limited at bush camps.</li>
  <li><strong>Memory cards:</strong> 128GB+ recommended. You will shoot thousands of photos.</li>
  <li><strong>Beanbag or window mount:</strong> For stabilizing your lens on the vehicle roof or window frame</li>
  <li><strong>Dust protection:</strong> Plastic bags, lens cloths, and a blower for sensor cleaning</li>
  <li><strong>Binoculars:</strong> 8x42 or 10x42 are ideal for spotting distant wildlife and birds</li>
</ul>
<p>Even if you are shooting on a smartphone, modern phones with zoom lenses can capture excellent safari images. A small phone tripod adapter and a pocket binocular can significantly improve your results.</p>

<h2>Health and Safety Items</h2>
<ul>
  <li><strong>Prescription medications</strong> — bring more than you need in case of delays</li>
  <li><strong>Antimalarial medication</strong> — consult your doctor at least 4-6 weeks before travel. See our <a href="blog-post.php?slug=tanzania-visa-requirements-vaccinations">Tanzania health requirements guide</a>.</li>
  <li><strong>Insect repellent</strong> — DEET 30%+ or picaridin-based. Essential for evenings.</li>
  <li><strong>Sunscreen</strong> — SPF 50+. The equatorial sun is intense, especially at altitude.</li>
  <li><strong>Basic first aid kit</strong> — plasters, antiseptic, pain relievers, anti-diarrhea medication</li>
  <li><strong>Hand sanitizer</strong> — useful on game drives when hand-washing is not available</li>
  <li><strong>Rehydration salts</strong> — for the heat and long game drives</li>
  <li><strong>Lip balm with SPF</strong> — often overlooked but essential</li>
</ul>

<h2>Documents and Money</h2>
<ul>
  <li><strong>Passport</strong> — valid for at least 6 months beyond your travel dates with at least 2 blank pages</li>
  <li><strong>Tanzania visa</strong> — apply online via e-visa or obtain on arrival ($50 single entry)</li>
  <li><strong>Yellow fever certificate</strong> — required if arriving from an endemic country</li>
  <li><strong>Travel insurance documents</strong> — with medical evacuation coverage (strongly recommended)</li>
  <li><strong>Printed copies of bookings</strong> — hotel confirmations, safari itinerary, flight tickets</li>
  <li><strong>US Dollars</strong> — for tips, visa on arrival, and small purchases. Bring crisp, post-2006 bills.</li>
  <li><strong>Credit/debit card</strong> — Visa is most widely accepted in Tanzania. ATMs available in Arusha.</li>
</ul>

<h2>Other Safari Essentials</h2>
<ul>
  <li><strong>Headlamp or small flashlight</strong> — camps and lodges may have limited lighting at night</li>
  <li><strong>Power bank</strong> — for charging devices during long game drives</li>
  <li><strong>Universal power adapter</strong> — Tanzania uses UK-type G plugs (3-pin)</li>
  <li><strong>Dry bag or ziplock bags</strong> — to protect electronics from dust and occasional rain</li>
  <li><strong>Reusable water bottle</strong> — most safari vehicles carry a cooler with water, but having your own is convenient</li>
  <li><strong>Small daypack</strong> — for game drives and nature walks to carry essentials</li>
</ul>

<h2>Season-Specific Packing Tips</h2>
<p><strong>Dry season (June-October):</strong> Dusty conditions — bring extra lens cloths, a buff/bandana for your face, and darker neutral clothing that shows dust less. Mornings can be cool (10-15°C), especially at altitude.</p>
<p><strong>Green season (November-May):</strong> Pack a quality rain jacket, waterproof camera bag protection, and mosquito repellent. Roads may be muddy. The upside: lush, photogenic landscapes and dramatic storm skies.</p>

